              <br />
              <p class="rem2 font-weight-bold">เทคโนโลยีของเครื่องพิมพ์นั้นแบ่งออกเป็น 4 กลุ่มได้แก่</p>
              <p class="rem5">1. ใช้ผงหมึก ( Toner-based printers)</p>
              <p class="rem5">2. พ่นน้ำหมึก (Liquid inkjet printer)</p>
              <p class="rem5">3. การระเหิด (Dye-sublimation printers)</p>
              <p class="rem5">4. ใช้ความร้อน (Thermal printers)</p>
              <p class="rem5">5. เครื่องพิมพ์แบบกระแทก ( Impact printers)</p>

              <h4>การพิมพ์ด้วยผงหมึก (Toner-based printers)</h4>
              <p class="rem2 text-justify">
                การพิมพ์ด้วยผงหมึก (Toner-based printers)
                เป็นเครื่องพิมพ์ที่สร้างภาพบนกระดาษโดยใช้ผงหมึก เรียกว่าโทนเนอร์
                (toner) สามารถพิมพ์ได้อย่างรวดเร็ว และมีคุณภาพการพิมพ์ที่ดี
                หลักการทำงานจะคล้ายกับเครื่องถ่ายเอกสาร
                เครื่องพิมพ์ประเภทนี้ที่ได้รับความนิยมคือ เครื่องพิมพ์แบบเลเซอร์
                (LASER printer) ซึ่งมีส่วนประกอบสำคัญอยู่ 5 ส่วนคือ
              </p>
              <p class="rem5">- ดรัม (drum) เป็นลูกกลิ้งไวต่อประจุ ประจุและผงหมึกสามารถยึดเกาะได้ดี</p>
              <p class="rem5">- โทนเนอร์ (toner) เป็นผงหมึกที่เกิดประจุและเกาะกับดรัมได้ดี</p>
              <p class="rem5">- เลเซอร์ไดโอด ( later diode) ใช้สร้างแสงเลเซอร์</p>
              <p class="rem5">- ลูกกลิ้งสำหรับอบหลอมและรีดผงหมึก (fuser)</p>
              <br />
              <div class="text-center font-italic col">
                <img src="/img/lesson7/9.png" width="70%" />
                <p>
                  <br />รูป 9 โครงสร้างตัดขวางของเครื่องพิมพ์แบบเลเซอร์
                </p>
              </div>
              <br />
              <p class="rem2 font-weight-bold">การทำงานของเครื่องพิมพ์แบบเลเซอร์จะมีขั้นตอนดังนี้</p>
              <p
                class="rem5"
              >1. ทำความสะอาดดรัม(Clean): ลูกกลิ้งดรัมมีความสำคัญมากในการสร้างภาพบนกระดาษ เพราะมีหน้าที่ลำเลียงผงหมึกจากโทนเนอร์ไปยังกระดาษ หากมีผงหมึกตกค้างจากการพิมพ์ครั้งก่อน จะทำให้เกิดเป็นรอยสกปรกที่กระดาษได้ ดังนั้นขั้นตอนแรกของการพิมพ์คือการทำความสะอาดดรัมเสียก่อน</p>
              <p
                class="rem5"
              >2. ถ่ายเทประจุลงไปยังดรัม (Charge): ด้านข้างของดรัมจะมีเส้นลวดโคโรน่า (corona wire) พาดขนานอยู่ เมื่อจ่ายไฟฟ้าแรงดันสูงเข้าไปในเส้นลวดนี้ อากาศรอบ ๆจะกลายเป็นไอออน และประจุไอออนนี้จะเกาะติดอยู่กับดรัม ผิวของดรัมที่ผ่านขั้นตอนนี้ จะมีประจุเกาะอยู่จนเต็มพื้นที่</p>
              <p
                class="rem5"
              >3. เปิดรับแสง (Exposure) : ภาพที่ต้องการพิมพ์จะถูกวาดลงไปบนดรัมด้วยแสงเลเซอร์ โดยการกวาด(แรสเตอร์ไรเซชั่น) ด้วยกระจกหกด้านที่หมุนด้วยความเร็วสูง การหมุนของกระจกจะทำให้ลำแสงกวาดไปในแนวนอน ประกอบกับการเปิดและปิด เลเซอร์ ในจังหวะที่เหมาะสมตามข้อมูลแผนที่บิต ก็จะทำให้เกิดเป็นภาพขึ้นที่ดรัม เนื่องจากประจุที่ถูกแสงเลเซอร์ตกกระทบ จะหลุดออกไปจากดรัม ดังนั้นภาพที่เกิดขึ้นบนดรัมคือประจุที่เรียงตัวเป็นภาพเนกาตีฟ (negative) และกลับซ้ายขวา(mirror image) ของภาพที่ต้องการพิมพ์</p>
              <p
                class="rem5"
              >4. ทำให้เกิดภาพ (Develop): ขั้นตอนนี้ผงหมึกจะถูกทำให้มีประจุเป็นลบ (ประจุชนิดเดียวกับบนดรัม) และผงหมึกที่เป็นลบนี้จะถูกถ่ายเทผ่านลูกกลิ้งลงไปบนดรัม และจะไปเกาะอยู่บนดรัมในส่วนที่ไม่มีประจุ (ส่วนที่ถูกฉายด้วยเลเซอร์) เนื่องจากประจุเป็นแบบเดียวกันจะไม่ดึงดูดกัน ส่วนที่ไม่ถูกแสงจึงไม่มีผงหมึกเกาะ</p>
              <p
                class="rem5"
              >5. ส่งผ่านผงหมึกลงกระดาษ: ผงหมึกที่เกาะอยู่บนดรัมจะถูกกดลงบนกระดาษที่ต้องการพิมพ์จึงเกิดเป็นภาพขึ้นบนกระดาษ ภาพบนกระดาษที่ผ่านขั้นตอนนี้จะเกิดเป็นภาพที่ต้องการพิมพ์ แต่ผงหมึกจะยังไม่ยึดติดกับกระดาษอย่างถาวร</p>
              <p
                class="rem5"
              >6. หลอมหมึก: ผงหมึกที่ติดอยู่บนกระดาษจะถูกอบด้วยความร้อนให้หลอมและรีดให้ติดแน่นถาวรอยู่บนกระดาษ ก็เสร็จสิ้นขั้นตอนของการพิมพ์แบบเลเซอร์</p>
