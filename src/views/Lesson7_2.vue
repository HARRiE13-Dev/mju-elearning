               <br>
                <h4>พ่นน้ำหมึก (Liquid inkjet printer)</h4>

                <p class="rem2">การพิมพ์ด้วยการพ่นน้ำหมึกนั้น เป็นการพิมพ์ภาพลงบนกระดาษด้วยวิธีการพ่นพ่นน้ำหมึกซึ่งเป็นของเหลวลงบนกระดาษหรือพื้นผิวที่ต้องการพิมพ์ เครื่องพิมพ์ประเภทนี้ เป็นเครื่องพิมพ์ที่ได้รับความนิยมสูงที่สุดเนื่องจากมีราคาที่ถูก คุณภาพการพิมพ์ที่ดี มีความละเอียดสูงและเสียงไม่ดังมาก ส่วนประกอบสำคัญคือหัวพิมพ์ซึ่งทำหน้าที่พ่นน้ำหมึกลงเป็นภาพที่ต้องการ</p>
              <br />
              <div class="text-center font-italic col">
                <img src="/img/lesson7/13.png" width="40%" />
                <p>
                  <br />รูป 13 หัวพิมพ์ของเครื่องพิมพ์แบบพ่นน้ำหมึก (By Hans-Jürgen Räth Akrisios - Own work, Public Domain)
                </p>
              </div>
              <br />
              <p class="rem2 font-weight-bold">  เทคโนโลยีในการพ่นหมึกนั้นมีอยู่ 2 แบบคือ </p>
              <p class="rem5">1. การพ่นแบบต่อเนื่อง (continuous inkjet: CIJ) จะมีการพ่นน้ำหมึกตลอดเวลา โดยใช้สนามไฟฟ้าควบคุมทิศทางของการแรสเตอไรเซชั่น การพิมพ์ไม่ต้องสัมผัสกับวัตถุที่ต้องการพิมพ์ สามารถพิมพ์ได้ความเร็วสูง แต่คุณภาพไม่ละเอียดจึงนิยมใช้ในโรงงานอุตสาหกรรม เช่น การพิมพ์วันหมดอายุบนฉลากสินค้า</p>
              <p class="rem5">2. พ่นหมึกตามความต้องการ ( drop-on-demand: DOD) คือเทคโนโลยีการพ่นน้ำหมึกที่ใช้ในเครื่องพิมพ์สำนักงาน หัวพิมพ์ของระบบนี้จะประกอบไปด้วยหัวพ่นน้ำหมึกขนาดเล็กเป็นจำนวนมาก ทำให้พิมพ์ได้ทีละหลายจุดภาพ ต่อการกวาด 1 ครั้ง หัวพ่นแต่ละหัวสามารถควบคุมการปล่อยหมึกได้อย่างอิสระเพื่อสร้างจุดภาพได้ตามต้องการ เทคโนโลยีในการควบคุมการปล่อยน้ำหมึกตามที่ต้องการนั้นแบ่งออกเป็น 2 กลุ่มคือ</p>
              <p class="rem7"> 2.1  หัวพิมพ์แบบเปียโซอิเล็กทริก (Piezoelectric DOD) วัสดุที่มีคุณสมบัติเปียโซอิเล็กทริกจะเกิดการสั่นหรือปิดตัวเมื่อจ่ายกระแสไฟฟ้าเข้าไปในตัววัสดุ มีหลักการทำงานคล้ายกระบอกฉีดยาโดยใช้วัสดุเปียโซอิเล็กทริกเป็นตัวดันน้ำหมึกให้พ่นออกมายังหัวฉีด เหมือนกับมีปั๊มขนาดจิ๋วที่สั่งให้พ่นน้ำหมึกออกมาได้ในเวลาที่ต้องการ และยังสามารถสร้างหยดหมึกได้หลายขนาดอีกด้วย เนื่องจากใช้กลไกเชิงกลในการควบคุมหมึก หัวพิมพ์แบบนี้จึงใช้ได้กับน้ำหมึกที่มีเคมีหลากหลาย จึงมีหมึกให้เลือกใช้ได้หลายแบบ เครื่องพิมพ์ที่ใช้หัวพิมพ์แบบนี้คือ Epson และ Brother</p>
              <br />
              <div class="text-center font-italic col">
                <img src="/img/lesson7/14.png" width="70%" />
                <p>
                  <br />รูป 14 โครงสร้างหัวพิมพ์แบบเปียโซอิเล็กทริก
                </p>
              </div>
              <br />
              <p class="rem7 text-justify">2.2 หัวพิมพ์แบบใช้ความร้อน (Thermal) หัวพิมพ์ชนิดนี้จะใช้น้ำหมึกชนิดพิเศษที่ถูกพัฒนามา
                  ให้เกิดฟองกากาศได้ง่ายเมื่อถูกความร้อน โครงสร้างของหัวพิมพ์จะคล้ายกับแบบเปียโซอิเล็กทริกแต่จะมีจุดที่แตกต่างกันคือ 
                  ไม่ได้ใช้วัสดุเปียโซในการดันน้ำหมึกให้ออกมาจากหัวฉีด แต่จะใช้อุปกรณ์ในการสร้างความร้อนเพื่อต้มน้ำหมึกแทน 
                  เมื่อต้องการปล่อยหยดหมึก หัวพิมพ์จะทำการให้ความร้อนยังตำแหน่งที่ต้องการหยดน้ำหมึก
                   เมื่อหมึกได้รับความร้อนก็จะเกิดฟองอากาศ ฟองนี้จะไปเพิ่มความดันในหัวพ่นทำให้ไปดันน้ำหมึกออกมาจากหัวพิมพ์
                    อุปกรณ์ในการสร้างความร้อนนิยมใช้ตัวต้านทานแบบฟิล์มบาง (thin-film resistors)
                     หัวพิมพ์แบบนี้ไม่สามารถปรับขนาดของหยุดหมึกได้ แต่สามารถผลิตหัวพ่นให้มีขนาดเล็กกว่าแบบเปียโซอิเล็กทริกได้
                      จึงสร้างจุดภาพได้เล็กกว่าคุณภาพการพิมพ์จึงละเอียดกว่า แต่การที่ต้องใช้หมึกชนิดพิเศษทำให้มีชนิดของหมึกให้เลือกน้อย
                       หมึกส่วนใหญ่ไม่กันน้ำ และหมึกมีความเข้มข้นน้อยกว่าแบบเปียโซอิเล็กทริก เครื่องพิมพ์ที่ใช้หัวพิมพ์แบบนี้คือ 
                       Hewlett-Packard Canon และ Lexmark</p>
                <br />
              <div class="text-center font-italic col">
                <img src="/img/lesson7/15.png" width="70%" />
                <p>
                  <br />รูป 15 โครงสร้างหัวพิมพ์แบบใช้ความร้อน
                </p>
              </div>
              <br />
              <p class="rem2 text-justify"> เครื่องพิมพ์แบบพ่นหมึกมีความละเอียดในการพิมพ์สูงกว่าแบบเลเซอร์ แต่ในการพิมพ์จะใช้เวลานานกว่า 
                  เครื่องพิมพ์แบบพ่นน้ำหมึก มีโครงสร้างที่ไม่ซับซ้อนจึงดูแลรักษาง่าย กระดาษติดได้ยาก
                   ไม่เกิดความร้อนในขนะพิมพ์ทำให้สามารถพิมพ์บนวัสดุที่หลากหลาย อย่างเช่นสติกเกอร์และแผ่นซีดีรอม 
                   คุณภาพการพิมพ์สูงพอที่จะพิมพ์ภาพถ่ายได้ แต่ข้อเสียคือ ต้องใช้หมึกแบบพิเศษจึงมีราคาสูงทำให้ต้นทุนใน
                   การพิมพ์ต่อแผ่นมีราคาสูง การพิมพ์ใช้หมึกที่เป็นของเหลวทำให้ต้องใช้เวลารอให้แห้งนาน
                    และไม่สามารถพิมพ์ลงกระดาษที่บางได้ เพราะหมึกจะทะลุกระดาษลงไปยังหน้าที่อยู่อีกด้านได้ 
                    ภาพสีที่ได้จากการพิมพ์ระบบนี้จะซีดจางได้ง่าย แต่ป้องกันได้โดยใช้หมึกชนิดที่ป้องกันการซีดจางเมื่อถูกแสง
                     แต่ก็จะมีราคาสูงขึ้นด้วย ปัญหาอีกประการของเครื่องพิมพ์ระบบนี้คือ หัวพ่นหมึกมีขนาดเล็กมากทำให้ง่ายต่อการอุดตัน
                      ดังนั้นผู้ใช้งานไม่ควรปิดเครื่องทิ้งไว้นานเพราะจะทำให้หัวพ่นอุดตันได้ 
                      โดยทั่วไปแล้วเครื่องพิมพ์จะทำการล้างหัวพิมพ์โดยอัตโนมัติเองวันละ 1 ครั้ง แต่หากผู้ใช้ปิดเครื่องไว้นานจนพบว่าหัวพ่นตัน 
                      (เมื่อหัวพ่นตัน จะพิมพ์จุดภาพได้ไม่ครบ) ให้ทำการสั่งล้างหัวพ่นผ่านทางซอร์ฟแวร์ไดรเวอร์ของเครื่องพิมพ์ 
                      จนกว่าคุณภาพการพิมพ์จะสมบูรณ์</p>
                 <br />
              <div class="text-center font-italic col">
                <img src="/img/lesson7/16.png" width="70%" />
                <p>
                  <br />รูป 16 เครื่องพิมพ์แบบพ่นน้ำหมึก (ภาพโดย Darwin Laganzon จาก Pixabay)
                </p>
              </div>
              <br />
              <p class="rem2">ปัจจุบันได้มีการดัดแปลงหัวพิมพ์โดยการเติมหมึกเข้าไปใหม่ หรือการนำไปติดแทงค์หมึก 
                  ซึ่งก็สามารถทำได้ แต่จะทำให้เครื่องหมดประกัน แต่หากจำเป็นต้องเติมหมึก ให้ใช้หมึกที่ออกแบบมาตรงกับชนิดของเครื่องพิมพ์ 
                  เนื่องจากหัวพิมพ์ที่แตกต่างกันจะใช้หมึกที่มีเคมีแตกต่างกัน จะใช้หมึกแทนกันไม่ได้ 
                  หากใช้หมึกผิดประเภทจะทำให้หัวพิมพ์เสียหายได้</p>
