              <p
                class="rem2"
              >เนื่องจากเครื่องพิมพ์จะชนิดนี้จะต้องนำเอาข้อมูลภาพที่ส่งมาจากเครื่องคอมพิวเตอร์มาสร้างเป็นภาพที่กลับด้านซ้าย-ขวา และการพิมพ์จะไม่ทำทีละกลุ่มจุดภาพ แต่จะกวาดทีละเส้นของทุก ๆจุดภาพในแนวนอน ดังนั้นเครื่องพิมพ์จึงต้องบันทึกข้อมูลที่ส่งมาจากเครื่องคอมพิวเตอร์ให้ครบทั้งหน้ากระดาษก่อนในหน่วยความจำของเครื่องพิมพ์ (buffer) จึงจะสามารถประมวลผลและเริ่มพิมพ์ได้ในขั้นตอนนี้หากการเชื่อมต่อมีความเร็วต่ำตัวอย่างเช่นการเชื่อมต่อผ่านทางเครือข่ายไร้สาย (WIFI) อาจต้องใช้เวลานานจึงจะเริ่มทำการพิมพ์ได้</p>
              <p class="rem2">ในการใช้งานผู้ใช้จะต้องทำการติดตั้งซอฟต์แวร์ไดรเวอร์ที่ให้มาพร้อมกับเครื่องพิมพ์ให้ตรงตามรุ่น เนื่องจากเครื่องพิมพ์จากผู้ผลิตแตกต่างกัน จะใช้ภาษาในการควมคุมแรสเตอไรเซชั่นที่แตกต่างกัน แต่หากไม่สามารถหาได้ ให้ทำการตรวจสอบจากคู่มือว่าเครื่องพิมพ์รองรับภาษาแรสเตอร์ใดบ้าง โดยหลักๆจะมีอยู่ 3 ภาษาคือ</p>

              <p class="rem5"> •	Adobe Postscript:PS หรือ โพสสคริปปริ้นเตอร์</p>
              <p class="rem5"> •	HP Printer Command Language: PCL หรือ พีซีแอลปริ้นเตอร์</p>
              <p class="rem5"> •	Microsoft XML Page Specification: XPS หรือเอ็กพีเอสปร๊นเตอร์</p>

              <p class="rem2">ให้ทำการดาวน์โหลดไดรฟเวอร์ยูนิเวอร์แซล (universal driver or unified driver) มาจากผู้ผลิตมาทำการติดตั้ง แล้วทำการเลือกภาษาควบคุมให้ถูกต้องตามรุ่นของเครื่องพิมพ์ ก็จะสามารถใช้แทนกันได้</p>

              <p class="rem2">เครื่องพิมพ์ระบบนี้มีหลักการทำงานที่ซับซ้อน มีชิ้นส่วนการทำงานเป็นจำนวนมาก การทำงานจึงมีเสียงดัง และกระดาษจะติดในระบบได้ง่าย (paper jam) ผงหมึกมีความละเอียดและสามารถฟุ้งกระจายในอากาศได้ง่ายจึงควรติดตั้งในบริเวณที่มีอากาศถ่ายเทได้ดี กระดาษที่ใช้ต้องไม่มีสิ่งแปลกปลอม นำกระดาษที่ใช้แล้วมาใช้ซ้ำต้องระวังไม่ให้มีลวดเย็บกระดาษติดอยู่ เนื่องจากจะทำให้ดรัมเสียหายได้ เครื่องพิมพ์ชนิดนี้กระดาษจะต้องถูกอบด้วยความร้อนสูง จึงไม่ควรใช้พิมพ์บนวัสดุที่ไม่ทนความร้อน ตัวอย่างเช่นสติกเกอร์พลาสติก หรือแผนใสที่ไม่ทนความร้อน และเมื่อเริ่มทำการพิมพ์แล้วจะไม่สามารถหยุดการทำงานชั่วคราวได้จะต้องพิมพ์จนหมดหน้ากระดาษเพราะการหยุดกระดาษจะทำให้กระดาษไหม้ได้ </p>
              
