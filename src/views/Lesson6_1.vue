                หรือเพิ่มเติมสื่อได้ตามที่ตนเองต้องการ
                ซึ่งมีผลทำให้เว็บไซต์เหล่านั้น
                เป็นเว็บไซต์ที่ผู้ใช้งานมีส่วนร่วมในการสร้างสื่อ
                แทนที่จะเป็นเว็บไซต์ส่วนตัวของคนใดคนหนึ่ง
                ก็กลายเป็นเว็บไซต์ของกลุ่มคนกลุ่มหนึ่งขึ้นมา ทำให้เกิดเป็นชุมชน
                หรือสังคมย่อยๆของผู้ที่ใช้เว็บไซต์นั้นขึ้นมา
                โดยเนื้อหาหรือสื่อที่นำเสนอผ่านเว็บไซต์นั้น
                ก็เป็นสื่อที่คนในสังคมนั้นๆช่วยกันสร้างขึ้นมา
              </p>
