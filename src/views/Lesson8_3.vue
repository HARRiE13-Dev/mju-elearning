              <h5>หลักการป้องกันตนเองก่อนเข้าไปในโลกไซเบอร์</h5>
              <p class="rem2">
                1. การป้องกันข้อมูลส่วนตัว โดยการตั้งรหัสเข้าข้อมูลที่คาดเดายาก และใช้รหัสที่ต่างกัน ในแต่ละบริการหรือ
                บัญชีการเปลี่ยนรหัสผ่านบ่อยๆ หรือ การใช้ระบบสแกนลายนิ้วมือ ระบบตรวจสอบใบหน้า ก่อนเข้าสู่แอพ
                พลิเคชันออนไลน์
              </p>
              <p class="rem2">
                2. อย่าเข้าชมเว็บไซต์ที่คลิกเข้าไปจากอีเมล์ ข้อความหรือจากโซเชียล เน็ตเวิร์ก ต่างๆ ข้อความจากห้องแชท
                แบนเนอร์โฆษณาที่เป็นเว็บไซต์ที่น่าสงสัย ลิงค์ต่าง ๆ ที่ส่งมาจากคนที่คุณไม่รู้จัก
              </p>
              <p class="rem2">3. ระวังการสื่อสารปลอม ในการหลอกให้ส่งข้อมูลส่วนตัว</p>
              <p class="rem2">
                4. การตั้งค่าโปรแกรมค้นหาและกำจัดไวรัสคอมพิวเตอร์ เพิ่มความปลอดภัยเมื่อทำธุรกรรมทางธนาคาร และ
                หมั่นติดตามข่าวสารความเคลื่อนไหวของไวรัสอย่างสม่ำเสมอ
              </p>
              <p class="rem2">
                5. หลีกเลี่ยงการใช้คอมพิวเตอร์สาธารณะและสัญญาณไวไฟสาธารณะ เครื่องคอมพิวเตอร์สาธารณะอาจจะมี
                สปายแวร์อยู่จำนวนมาก ในสัญญาณไวไฟสาธารณะอาจมีความเสี่ยงในการถูกดักจับข้อมูลจากผู้ให้บริการ
                เครือข่ายหรือจากอาชญากรไซเบอร์ และอาจถูกโจมตีด้วยไวรัสเครือข่าย
              </p>
              <p
                class="rem2"
              >6. ก่อนเข้าเว็บไซต์ต้องตรวจสอบ URL ทุกครั้งควรเลือกเว็บไซต์ที่ปลอดภัยโดยสังเกตการใช้โปรโตคอลที่ขึ้นต้นด้วย 'https'</p>
              <p class="rem2">
                7. ถ้าเป็นไปได้อย่าใช้แอพพลิเคชันที่มีโฆษณาให้เรากดเปิดบราวเซอร์ โดยมากมักจะมาพร้อมกับแอพพลิเคชัน
                ฟรีซึ่งอาจจะแสดงลิงค์พาเราไปยังไซต์อันตรายได้
              </p>
              <p
                class="rem2"
              >8. หลีกเลี่ยงการเปิดเผยข้อมูลส่วนตัวบนพื้นที่สาธารณะ หากจำเป็นต้องโพส อย่าลืมมาลบทิ้งหลังจากเสร็จธุระ</p>
