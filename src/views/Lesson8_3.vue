              <h4>• การควบคุมรักษาความปลอดภัยทางข้อมูล ( Information Security Control)</h4>
              <p
                class="rem2"
              >การควบคุม คือ การสร้างระบบหรือมาตรการป้องกันความผิดพลาดที่อาจจะเกิดขึ้นกับข้อมูลสารสนเทศและระบบคอมพิวเตอร์จากภัยคุกคามทางคอมพิวเตอร์ การควบคุมการรักษาความปลอดภัยทางข้อมูลสามารถ ทำได้ดังนี้</p>
              <br />
              <h5>1. การควบคุมรักษาความปลอดภัยโดยตัวซอฟต์แวร์ (Software Control)</h5>
              <p
                class="rem2"
              >เป็นการป้องกันการลักลอบขโมยซอฟต์แวร์ การเข้าไปใช้ซอฟต์แวร์ในระบบโดยไม่ได้รับอนุญาต และการควบคุมความถูกต้องของการทำงานของซอฟต์แวร์ต่างๆ โดยมีระดับวิธีการ 3 ระดับ คือ</p>
              <p
                class="rem5"
              >1.1 การควบคุมจากระบบภายในของซอฟต์แวร์ (Internal Program Control) คือ การที่โปรแกรมนั้นได้มีการควบคุมสิทธิการเข้าถึง และสิทธิในการใช้ข้อมูลภายในระบบ ซึ่งถูกจัดเก็บไว้ในระบบฐานข้อมูลภายในระบบเอง</p>
              <p
                class="rem5"
              >1.2 การควบคุมความปลอดภัยโดยระบบปฏิบัติการ (Operating System Control) คือ การควบคุมสิทธิการเข้าถึงและการใช้ข้อมูลในส่วนต่างๆ ภายในระบบคอมพิวเตอร์ของผู้ใช้คนหนึ่ง และจำแนกแตกต่างจากผู้ใช้คนอื่น ๆ</p>
              <p
                class="rem5"
              >1.3 การควบคุมและการออกแบบโปรแกรม (Development Control) คือ การควบคุมตั้งแต่การออกแบบ การทดสอบก่อนการใช้งานจริงเพื่อให้ความมั่นใจว่าระบบที่พัฒนานั้นเป็นไปตามความต้องการของผู้ใช้ และเป็นการประเมินคุณภาพของระบบที่ทำการพัฒนา</p>
              <br />
              <h5>2. การควบคุมความปลอดภัยของระบบฮาร์ดแวร์ (Hardware Control)</h5>
              <p
                class="rem2"
              >เป็นการเลือกใช้เทคโนโลยีทางด้านฮาร์ดแวร์ที่สามารถควบคุมการเข้าถึงและป้องกันการทำงานผิดพลาดด้วยอุปกรณ์ภายในตัวเอง รวมทั้งมีระบบสำรองข้อมูลอย่างสม่ำเสมอเพื่อให้ระบบสามารถทำงานได้ตามปกติได้หากเกิดความผิดพลาดขึ้น โดยไม่เก็บข้อมูลไว้ที่เดียว ควรสำรองไว้ในอุปกรณ์อื่น เช่น แผ่นซีดี แผ่นวีดีโอ External Hard disk</p>
              <br />
              <h5>3. การใช้นโยบายในการควบคุม (Policies Control)</h5>
              <p
                class="rem2"
              >การประกาศใช้นโยบาย การตั้งกฎ ระเบียบ ในด้านการรักษาความปลอดภัยของระบบสารสนเทศโดยมีผลบังคับใช้ทั้งองค์กรเพื่อควบคุมการใช้งานของคนในองค์กร การละเมิดในระบบรักษาความปลอดภัยนั้นส่วนมากจะมีคนในองค์กรมีส่วนเกี่ยวข้องโดยเสมอดังนั้นจึงขาดไม่ได้ที่จะต้องมีมาตรการทางด้านระเบียบกฎเกณฑ์มารองรับหรือควบคุมการใช้งาน (Authentication) ของบุคลากรภายในองค์กร ตลอดไปถึงการออกกฎหมายด้านการรักษาความปลอดภัยของประเทศ เพื่อป้องกันอาชญากรรมคอมพิวเตอร์ที่จะเกิดขึ้นในสังคมปัจจุบัน เช่น การบังคับให้มีการเปลี่ยนรหัสการเข้าใช้ (Password) ระบบสารสนเทศชองหน่วยงานทุกๆ เดือน หรือ สัปดาห์ เพื่อป้องกันการขโมยรหัส การกำหนดให้มีการบันทึกการใช้งาน (Log) ของระบบสารสนเทศเพื่อใช้ในการตรวจสอบ (Audit) และหาผู้ละเมิดมาลงโทษ</p>
              <br />
              <h5>4. การควบคุมความปลอดภัยทางกายภาพ (Physical Control)</h5>
              <p
                class="rem2"
              >เป็นการกำหนดมาตรการรักษาความปลอดภัยทั่วไปให้กับบุคคล สถานที่ และอุปกรณ์ ตลอดจนสื่อต่าง ๆ ที่บันทึกข้อมูลข่าวสาร เพื่อป้องกันไม่ให้ผู้บุกรุกเข้าถึงแหล่งข้อมูลได้ทางกายภาพ โดยหน่วยงานหรือองค์กรอาจกำหนดให้มีมาตรการการเข้าถึงศูนย์คอมพิวเตอร์และเครื่องคอมพิวเตอร์ที่สำคัญได้เฉพาะเจ้าหน้าที่ที่เกี่ยวข้องเท่านั้นเพื่อป้องกันขโมยฮาร์ดแวร์ หรือการเข้าไปใช้ห้องคอมพิวเตอร์โดยไม่ได้รับอนุญาต โดยการกำหนดสิทธิในการเข้าใช้ห้องเก็บข้อมูลหรือห้องคอมพิวเตอร์ การใช้อุปกรณ์ทางชีวภาพ (biometric device) ในการตรวจสอบลักษณะส่วนบุคคลก่อนเข้าใช้เช่น ตรวจสอบเสียง ลายนิ้วมือ ม่านตา รูปหน้า เป็นต้น การติดตั้งระบบรักษาความปลอดภัยด้วยระบบการรูดบัตรผ่านประตูควบคุมการเข้าออกห้องควบคุมคอมพิวเตอร์ การติดตั้งสัญญาณกันขโมยและกล้องวงจรปิดไว้ภายในห้องคอมพิวเตอร์ การใช้กุญแจล๊อกเครื่องคอมพิวเตอร์ หรือการใช้อุปกรณ์สำรองไฟ UPS เพื่อสามารถจัดเก็บสำรองข้อมูลได้ทันท่วงทีกรณีที่เกิดเหตุการณ์ไฟตก หรือไฟดับ</p>
