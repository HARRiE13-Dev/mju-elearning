              <h5>5. การควบคุมความปลอดภัยของข้อมูล (Data Security Control)</h5>
              <p
                class="rem2"
              >ข้อมูลเป็นสินทรัพย์สำคัญทางธุรกิจที่ต้องดูแลบำรุงรักษาและป้องกันอย่างดี การควบคุมรักษาความปลอดภัยของข้อมูลสารสนเทศ ได้แก่การควบคุมไม่ให้ผู้ไม่มีอำนาจหน้าที่เข้ามาดูข้อมูลของบริษัท และการป้องกันการลักลอบนำข้อมูลออกไปจากบริษัท โดยให้มีการบริหารจัดการให้ระบบข้อมูลมีลักษณะคงความเป็น C I A คือ</p>
              <p
                class="rem5"
              >1. การรักษาความลับ (Confidential) เป็นการดำเนินการควบคุมการเข้าถึงเรียกดูข้อมูลเพื่อให้สาระของข้อมูลข่าวสารได้ถูกเปิดเผยต่อบุคคล หรือ กระบวนการที่ได้รับอนุญาตเท่านั้น การป้องกันข้อมูลเป็นความลับต้องไม่เปิดเผยกับผู้ไม่มีสิทธิ มาตรการนี้จะทำได้โดยวิธีการเข้ารหัส (Cryptography) การใส่รหัส (Encryption) ให้กับข้อมูลที่ส่งผ่านทางระบบเครือข่ายเพื่อป้องกันการลักลอบขโมยข้อมูล หรือกำหนดสิทธิให้ผู้ใช้งานแต่ละคนสามารถใช้งานได้ตามสิทธิที่กำหนดเท่านั้น</p>
              <p
                class="rem5"
              >2. การคงสภาพ (Integrity) เป็นการควบคุมความถูกต้องครบถ้วนสมบูรณ์ของข้อมูล และวิธีการประมวลผล ต้องมีการควบคุมความผิดพลาด และเป็นการยืนยันว่าข้อมูลที่ต้องการรักษาไม่ถูกเปลี่ยนแปลงไปจากของเดิมโดยผู้ที่ไม่ได้รับอนุญาต เนื่องจากในบางกรณีผู้บุกรุกไม่มีความประสงค์ที่จะรู้สาระของข้อมูลข่าวสาร แต่ต้องการทำให้ข่าวสารนั้นผิดไปจากสาระเดิม เป็นต้น</p>
              <p
                class="rem5"
              >3. ความพร้อมในการใช้งาน (Availability) เป็นการควบคุมให้บุคคลผู้มีสิทธิ์เท่านั้นเข้าถึงข้อมูลได้ทุกเมื่อที่ต้องการ ต้องมีการควบคุมไม่ให้ระบบล้มเหลว มีสมรรถภาพทำงานต่อเนื่อง ไม่ให้ผู้ไม่มีสิทธิ์มาทำให้ระบบหยุดการทำงาน เป็นการรักษาความพร้อมในการใช้งานของข้อมูลเช่น ข้อมูลบัญชีเงินฝากของลูกค้าธนาคาร หรือข้อมูลสำคัญต่าง ๆ ที่ต้องพร้อมใช้งานในเวลาที่ต้องการ ซึ่งในบางครั้งเป็นข้อมูลที่สามารถเปิดเผยให้สาธารณชนรับทราบได้ เพื่อประโยชน์ในการประชาสัมพันธ์ หรือการเผยแพร่ในวงกว้าง เช่น ข้อมูลการท่องเที่ยว การแบ่งปันข้อมูล หรือ การติดต่อแบบโซเชียลเนตเวิรค์ (Social Network) เป็นต้น</p>
              <br />
              <h5>6. การควบคุมรักษาความปลอดภัยของระบบเครือข่าย (Network Security)</h5>
              <p
                class="rem2"
              >การรักษาความปลอดภัยของระบบเครือข่ายจะเกี่ยวข้องกับการควบคุมการเข้าใช้ข้อมูลต่างๆ (Access Control) ที่มีอยู่บนระบบเครือข่าย การรักษาความปลอดภัยของเครื่องเซิร์ฟเวอร์ การรักษาความปลอดภัยของการส่งข้อมูลผ่านเครือข่ายอินเตอร์เน็ต เช่น การใข้ระบบลายเซ็นดิจิตอล (Digital Signature) การเข้ารหัสเพื่อป้องกันการแก้ไขขัอมูลระหว่างทางการส่งข้อมูล การสร้างความน่าเชื่อถือของข้อมูลระหว่างจัดส่งด้วยใบรับรองดิจิตอล (Digital Certificate) รวมถึงการป้องกันการแพร่กระจายของไวรัสคอมพิวเตอร์ และสิ่งแปลกปลอมต่างๆ ที่เข้ามาทำลายระบบหรือเจาะระบบเพื่อต้องการขโมยข้อมูล เช่น สแปมเมล์ (spam mail) โฆษณา เป็นต้น การจัดการด้านความปลอดภัยของระบบเครือข่าย สามารถใช้ระบบ Firewall ที่เป็นฮาร์ดแวร์และซอฟต์แวร์ซึ่งเป็นเทคโนโลยีที่ทำการป้องกันผู้บุกรุกเข้า-ออกระบบ ซึ่งทำหน้าที่เปรียบเสมือนยามประตูค่อยตรวจสอบการเข้าออกระบบ รวมถึงการติดตั้งโปรแกรมป้องกันและกำจัดไวรัส (Anti-Virus) ที่มีการอัพเดตข้อมูลอย่างสม่ำเสมอ นอกจากนี้ยังใช้วิธีการใช้ iCloud การสำรองข้อมูลที่สำคัญเก็บเอาไว้ เพื่อใช้ในกรณีที่ข้อมูลเกิดความเสียหาย</p>
