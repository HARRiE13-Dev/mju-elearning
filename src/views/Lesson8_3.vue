              <h5>มารยาทในการใช้อินเทอร์เน็ต</h5>
              <p class="rem2">1. ไม่ใช้อินเทอร์เน็ตเพื่อการทำร้ายหรือรบกวนผู้อื่น</p>
              <p class="rem2">2. ไม่ใช้อินเทอร์เน็ตเพื่อการทำผิดกฎหมายหรือผิดศีลธรรม</p>
              <p class="rem2">3. ไม่เจาะระบบเครือข่ายอินเทอร์เน็ตของตนเองและผู้อื่น</p>
              <p
                class="rem2"
              >4. ไม่ใช้บัญชีอินเทอร์เน็ตของผู้อื่นและไม่ใช้เครือข่ายที่ไม่ได้รับอนุญาติ</p>
              <p
                class="rem2"
              >5. การติดต่อสื่อสารกับผู้อื่นบนเครือข่ายอินเทอร์เน็ต ต้องให้เกียรติซึ่งกันและกัน</p>
              <p
                class="rem2"
              >6. ต้องคำนึงถึงสิ่งที่เกิดขึ้นกับสังคมที่เกิดจากการกระทำของตน เช่น การโพสต์ การแชร์ข่าวสารปลอม</p>
