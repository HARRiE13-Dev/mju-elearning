              <h4>• กฎหมายเทคโนโลยีสารสนเทศของไทย</h4>
              <p
                class="rem2"
              >จากการใช้คอมพิวเตอร์ในทางที่ผิดกฎหมายและก่อให้เกิดความเสียหายต่อสังคม จึงจำเป็นต้องมีกฎหมายเทคโนโลยีสารสนเทศ สำหรับการใช้งานร่วมกันในสังคม ในแต่ละประเทศจึงได้มีการกำหนดระเบียบ กฎเกณฑ์ รวมถึงกฎหมายที่ใช้เป็นแนวทางในการปฏิบัติเพื่อให้เกิดคุณธรรมและจริยธรรมในการใช้เทคโนโลยีสารสนเทศหลายประเทศ เช่น สิงคโปร์ อินเดีย เกาหลีใต้ สหรัฐอเมริกา ฯลฯ ล้วนมีกฎหมายเทคโนโลยีสารสนเทศของตน แล้ว สำหรับประเทศไทย คณะรัฐมนตรีอนุมัติเมื่อวันที่ 15 ธันวาคม 2541 ให้กระทรวงวิทยาศาสตร์และเทคโนโลยี โดยศูนย์เทคโนโลยีอิเล็กทรอนิกส์และคอมพิวเตอร์แห่งชาติเป็นผู้ร่าง มีการกำหนดกฎหมายที่จะร่างทั้งสิ้นหกฉบับ</p>
              <p
                class="rem5"
              >1. กฎหมายธุรกรรมอิเล็กทรอนิกส์ เน้นในเรื่องข้อความที่จัดทำขึ้นเป็นรูปข้อมูลอิเล็กทรอนิกส์จะไม่ถูกปฏิเสธความมีผลทางกฎหมาย ถ้าข้อมูล ได้จัดทำขึ้นเป็นข้อมูลอิเล็กทรอนิกส์ โดยสามารถที่จะเข้าถึงเพื่อนำข้อความออกมาใช้ในภายหลังได้ ให้ถือว่า ข้อความนั้นได้ทำเป็นหนังสือ หรือมีหลักฐานเป็นหนังสือแล้ว (มาตรา 6 และมาตรา 7) โดยมีการพิสูจน์หลักฐานที่เป็น ข้อมูลอิเล็กทรอนิกส์และชั่งน้ำหนักพยานหลักฐาน (มาตรา 10) ข้อมูลเหล่านี้สามารถส่งและรับผ่านเครือข่ายเพื่อ รองรับวิทยาการสมัยใหม่</p>
              <p
                class="rem5"
              >2. กฎหมายลายมือชื่ออิเล็กทรอนิกส์ เน้นในเรื่องการใช้ลายมือชื่อดิจิตอล (digital signature) เพื่อยืนยันเอกสารหรือหลักฐาน และองค์กรที่ทำ หน้าที่ออกใบรับรองลายมือชื่อ การประกอบการรับรองลายมือชื่ออนุญาต ตลอดจนการกำกับการประกอบการรับรอง เพื่อให้ระบบลายมือชื่อดิจิตอลเป็นส่วนหนึ่งเสมือนการลงลายมือชื่อในเอกสาร</p>
              <p
                class="rem5"
              >3. กฎหมายอาชญากรรมทางคอมพิวเตอร์ เป็นกฎหมายที่เข้ามากำกับดูแลความสงบสุขของการใช้เทคโนโลยีสารสนเทศ เน้นในเรื่องสิทธิการใช้ การ ละเมิดสิทธิ์ โดยเฉพาะผู้บุกรุกหรือ hacker ที่ถือว่าเป็นการกระทำ การบุกรุก และเป็นอาชญากรรมทางคอมพิวเตอร์</p>
              <p
                class="rem5"
              >4. กฎหมายคุ้มครองข้อมูลส่วนบุคคล เนื่องจากข้อมูลในรูปดิจิตอลสามารถเผยแพร่และกระจายได้รวดเร็ว การส่งต่อ การกระจายข่าวสารอาจกระทบ ถึงสิทธิส่วนบุคคล กฎหมายจึงคุ้มครองข้อมูลส่วนบุคคล</p>
              <p
                class="rem5"
              >5. กฎหมายว่าด้วยการโอนเงินทางอิเล็กทรอนิกส์ เนื่องจากกิจการทางด้านการเงินและการโอนเงินทางอิเล็กทรอนิกส์ในรูปแบบที่เป็นทั้ง eCash eMoney หรือ การใช้เครดิตแลกเปลี่ยนกันจะมีบทบาท มีการใช้ตั๋วสัญญาใช้เงินแบบอิเล็กทรอนิกส์ ใช้รูปแบบเอกสารการเงิน อิเล็กทรอนิกส์ต่าง ๆ</p>
              <p
                class="rem5"
              >6. กฎหมายลำดับรองรัฐธรรมนูญมาตรา 78 ในมาตรา 78 แห่งรัฐธรรมนูญราชอาณาจักรไทยปัจจุบัน เน้นให้รัฐต้องกระจายอำนาจให้ท้องถิ่นพึ่งตนเองและ ตัดสินใจในกิจการท้องถิ่นได้เอง พัฒนาเศรษฐกิจท้องถิ่นและระบบสาธารณูปโภค และสาธารณูปการ ตลอดทั้ง โครงสร้างพื้นฐานสารสนเทศ ในท้องถิ่นให้ทั่วถึงและเท่าเทียมกันทั้งประเทศ</p>

              <br />
              <h4>• พ.ร.บ การรักษาความมั่นคงไซเบอร์</h4>
              <p
                class="rem2"
              >ในปัจจุบันการให้บริการหรือการประยุกต์ใช้เครือข่ายคอมพิวเตอร์ อินเทอร์เน็ต โครงข่ายโทรคมนาคม หรือการให้บริการโดยปกติของดาวเทียมมีความเสี่ยงจากภัยคุกคามทางไซเบอร์ซึ่งอาจกระทบต่อความมั่นคงของรัฐและความสงบเรียบร้อย ภายในประเทศ ดังนั้นเพื่อให้สามารถป้องกันหรือรับมือกับภัยคุกคามทางไซเบอร์ได้อย่างทันท่วงทีทั้งหน่วยงานของรัฐและหน่วยงานเอกชนจะต้องมีการป้องกัน รับมือ และลดความเสี่ยงจากภัยคุกคามทางไซเบอร์ไม่ให้เกิดผลกระทบต่อความมั่นคงในด้านต่างๆ หรือบริการที่สำคัญของประเทศ ไม่ว่าในสถานการณ์ทั่วไปหรือสถานการณ์อันเป็นภัยต่อความมั่นคงอย่างร้ายแรงก็ตาม ด้วยเหตุผลดังกล่าวสำนักงานพัฒนาธุรกรรมทางอิเล็กทรอนิกส์ สพธอ.( Electronic Transactions Development Agency , ETDA) กระทรวงดิจิทัลเพื่อเศรษฐกิจและสังคม (Ministry of Digital Economy and Society, DES) จึงได้มีกำหนดมาตราการแนวทางป้องกันการรับมือภัยคุกคามทางไซเบอร์และยกระดับการรักษาความมั่นคงปลอดภัย ด้วยใช้พ.ร.บ. การรักษาความมั่นคงปลอดภัยไซเบอร์ พ.ศ 2562 และมีผลบังคับใช้แล้ว ตั้งแต่วันที่ 28 พฤษภาคม พ.ศ. 2562 สามารถคลิกดาวน์โหลดและอ่านฉบับเต็มได้ที่ลิงค์นี้ พ.ร.บ การรักษาความมั่นคงไซเบอร์ พ.ศ 2562</p>
              <p
                class="rem2"
              >พ.ร.บ. การรักษาความมั่นคงปลอดภัยไซเบอร์ หรือ เรียกกันสั้นๆ ว่า พ.ร.บ.ไซเบอร์ คือ กฎหมายที่ตราขึ้นเพื่อให้ประเทศไทยมีมาตรการป้องกัน รับมือ และลดความเสี่ยงจากภัยคุกคามทางไซเบอร์ทั้งจากภายในและภายนอกประเทศ ที่กระทบต่อความมั่นคงของรัฐและความสงบเรียบร้อยภายในประเทศ โดย พ.ร.บ. นี้เริ่มบังคับใช้กับหน่วยงานโครงสร้างพื้นฐานสำคัญทางสารสนเทศ (Critical Information Infrastructure) ทั่วประเทศ หน่วยงานของรัฐและเอกชน โดยมีสาระสำคัญคือแนวทางในการจัดการ การป้องกัน การรับมือ และการลดความเสี่ยงทางไซเบอร์ มีการประสานความร่วมมือระหว่างผู้เกี่ยวข้อง พัฒนาความรู้ความสามารถของบุคคลากรและผู้เชี่ยวชาญ รวมถึงการให้ความรู้และความตระหนักถึงภัยไซเบอร์อีกด้วย</p>
              <h5 class="rem2">สรุปสาระสำคัญกฎหมายว่าด้วยการรักษาความมั่นคงปลอดภัยไซเบอร์ ดังนี้</h5>
              <p
                class="rem5"
              >1. กำหนดให้โครงสร้างพื้นฐานสำคัญทางสารสนเทศและหน่วยงานภาครัฐมีมาตรฐานและมีแนวทางปฏิบัติในการรักษาความมั่นคงปลอดภัยไซเบอร์</p>
              <p
                class="rem5"
              >2. การเฝ้าระวังภัยคุกคามและมีแผนรีบมือเพื่อกู้คืนระบบให้กลับมาทำงานได้ปกติได้อย่างรวดเร็ว</p>
              <p
                class="rem5"
              >3. มีการร่วมมือและประสานงานกันกับสำนักงานรักษาความปลอดภัยไซเบอร์เมื่อมีภัยร้ายแรงที่ทำให้บริการที่สำคัญไม่สามารถทำงานได้ จนทำให้ประชาชนเดือนร้อน เข้ามาให้ความช่วยเหลือแก่หน่วยงาน</p>
              <p class="rem5 font-weight-bold">โดยภัยคุกคามสามารถแบ่งเป็น 3 ระดับ ดังนี้</p>
              <p
                class="rem7"
              >- ภัยคุกคามทางไซเบอร์ในระดับไม่ร้ายแรง คือ หตุการณ์ที่เป็นภัยคุกคามมีผลทำให้ประสิทธิภาพระบบเครือข่าย หรือคอมพิวเตอร์ทำให้ช้าลง</p>
              <p
                class="rem7"
              >- ภัยคุกคามทางไซเบอร์ในระดับร้ายแรง คือภัยคุกคามที่โจมตีระบบคอมพิวเตอร์โดยกระทบต่อความมั่นคงของรัฐทั้งด้านความสัมพันธ์ระหว่างประเทศ การป้องกันประเทศ เศรษฐกิจ ความปลอดภัยสาธารณะ</p>
              <p
                class="rem7"
              >- ภัยคุกคามทางไซเบอร์ในระดับวิกฤติ คือภัยคุมคามที่มีลักษณะการโจมตีระบบคอมพิวเตอร์ที่ส่งผลกระทบรุนแรงต่อโครงสร้างพื้นฐานของระบบสารสนเทศภายในประเทศเป็นบริเวณกว้าง และการโจมตีที่เป็นการก่ออาชญากรรมทางไซเบอร์</p>
              <br />
              <h4>คณะกรรมการที่มีส่วนเกี่ยวข้องโดยตรง</h4>
              <p
                class="rem2"
              >1. คณะกรรมการการรักษาความมั่นคง ปลอดภัยไซเบอร์แห่งชาติ เรียกย่อๆ ว่า กมช. และให้ใช้ชื่อเป็นภาษาอังกฤษว่า National Cyber Security Committee เรียกโดยย่อว่า NCSC มีหน้าที่ กําหนด เสนอ จัดทําแผนปฏิบัติกําหนด มาตรการและแนวทางต่าง ๆ ที่มีส่วนเกี่ยวข้องกับ พ.ร.บ.</p>
              <p
                class="rem2"
              >2. คณะกรรมการกํากับดูแลด้านความมั่นคงปลอดภัยไซเบอร์ เรียกย่อๆ ว่า กกม. มีหน้าที่ กํากับดูแลการดําเนินงานของศูนย์ประสานการรักษาความมั่นคงปลอดภัยระบบ คอมพิวเตอร์แห่งชาติ และการเผชิญเหตุและนิติวิทยาศาสตร์ทางคอมพิวเตอร์ รวมถึงกําหนดระดับของภัยคุกคามทางไซเบอร์ พร้อมทั้งรายละเอียดของมาตรการป้องกัน</p>
              <h4>สรุปบทลงโทษ</h4>
              <p
                class="rem2"
              >1. หากพนักงานเจ้าหน้าที่ตามพระราชบัญญัตินี้เปิดเผยหรือส่งมอบข้อมูลคอมพิวเตอร์ ข้อมูลจราจรทางคอมพิวเตอร์ ข้อมูลอื่นที่เกี่ยวข้องกับระบบคอมพิวเตอร์ หรือข้อมูลของผู้ใช้บริการ ที่ได้มาตามพระราชบัญญัตินี้ให้แก่บุคคลใด ผู้ใดฝ่าฝืนต้องระวางโทษจำคุกไม่เกิน 3 ปี หรือปรับไม่เกินหกหมื่นบาท หรือทั้งจำทั้งปรับตามมาตรา 70</p>
              <p
                class="rem2"
              >2. หากพนักงานเจ้าหน้าที่ตามพระราชบัญญัตินี้ผู้ใดกระทำโดยประมาทเป็นเหตุให้ผู้อื่นล่วงรู้ข้อมูลคอมพิวเตอร์ ข้อมูลจราจรทางคอมพิวเตอร์ ข้อมูลของผู้ใช้บริการหรือข้อมูลอื่นที่เกี่ยวข้องกับระบบคอมพิวเตอร์ ที่ได้มาตามพระราชบัญญัตินี้ ต้องระวางโทษจำคุกไม่เกินหนึ่งปี หรือปรับไม่เกิน 20,000 บาท หรือทั้งจำทั้งปรับตามมาตรา 71</p>
              <p
                class="rem2"
              >3. หากผู้ใดล่วงรู้ข้อมูลคอมพิวเตอร์ ข้อมูลจราจรทางคอมพิวเตอร์ ข้อมูลของผู้ใช้บริการ หรือข้อมูลอื่นที่เกี่ยวข้องกับระบบคอมพิวเตอร์ ที่พนักงานเจ้าหน้าที่ได้มาตามพระราชบัญญัตินี้ และเปิดเผยข้อมูลนั้นต่อผู้หนึ่งผู้ใดโดยมิชอบ ต้องระวางโทษจำคุกไม่เกิน 2 ปี หรือปรับไม่เกิน 40,000 บาท หรือทั้งจำทั้งปรับตามมาตรา 72</p>
              <p
                class="rem2"
              >4. หน่วยงานโครงสร้างพื้นฐานสำคัญทางสารสนเทศใดไม่รายงานเหตุภัยคุกคามทางไซเบอร์ตามมาตรา 57โดยไม่มีเหตุอันสมควร ต้องระวางโทษปรับไม่เกิน 200,000 บาท ตามมาตรา 73</p>
              <p
                class="rem2"
              >5. ผู้ใดไม่ปฏิบัติตามหนังสือเรียกของพนักงานเจ้าหน้าที่หรือไม่ส่งข้อมูลให้แก่พนักงานเจ้าหน้าที่ตามมาตรา 61 (1) หรือ (2) โดยไม่มีเหตุอันสมควรแล้วแต่กรณี ต้องระวางโทษปรับไม่เกิน 100,000 บาทตามมาตรา 74</p>
              <p
                class="rem2"
              >6. ผู้ใดฝ่าฝืนหรือไม่ปฏิบัติตามคำสั่งของ กกม. ตามมาตรา 65 (1) และ (2) โดยไม่มีเหตุอันสมควร ต้องระวางโทษปรับไม่เกิน 300,000 บาท และปรับอีกไม่เกินวันละ 10,000 บาท นับแต่วันที่ครบกำหนดระยะเวลาที่ กกม. ออกคำสั่งให้ปฏิบัติจนกว่าจะปฏิบัติให้ถูกต้องผู้ใดฝ่าฝืนหรือไม่ปฏิบัติตามคำสั่งของ กกม. ตามมาตรา 65 (3) และ (4) หรือไม่ปฏิบัติตามคำสั่งศาลตามมาตรา 65 (5) ต้องระวางโทษจำคุกไม่เกินหนึ่งปี หรือปรับไม่เกิน 20,000 บาท หรือทั้งจำทั้งปรับตามมาตรา 75</p>
              <p
                class="rem2"
              >7. ผู้ใดขัดขวาง หรือไม่ปฏิบัติตามคำสั่งของ กกม. หรือพนักงานเจ้าหน้าที่ซึ่งปฏิบัติการตามคำสั่งของ กกม. ตามมาตรา 66 (1) หรือไม่ปฏิบัติตามคำสั่งศาลตามมาตรา 66 (2)(3) หรือ (4) โดยไม่มีเหตุอันสมควร ต้องระวางโทษจ าคุกไม่เกินสามปี หรือปรับไม่เกินหกหมื่นบาทหรือทั้งจำทั้งปรับตามมาตรา 76</p>
              <p
                class="rem2"
              >8. ในกรณีที่ผู้กระทำความผิดตามพระราชบัญญัตินี้เป็นนิติบุคคล ถ้าการกระทำความผิดของนิติบุคคลนั้นเกิดจากการสั่งการหรือการกระทำของกรรมการหรือผู้จัดการ หรือบุคคลใดซึ่งรับผิดชอบในการดำเนินงานของนิติบุคคลนั้น หรือในกรณีที่บุคคลดังกล่าวมีหน้าที่ต้องสั่งการหรือกระทำการและละเว้นไม่สั่งการหรือไม่กระทำการจนเป็นเหตุให้นิติบุคคลนั้นกระทำความผิด ผู้นั้นต้องรับโทษตามที่บัญญัติไว้สำหรับความผิดนั้น ๆ ด้วยตามมาตรา 77</p>
              <br />
              <h4>• พ.ร.บ. คุ้มครองข้อมูลส่วนบุคคล</h4>
              <p
                class="rem2"
              >พ.ร.บ. คุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 เป็นการคุ้มครองข้อมูลส่วนบุคคลให้มีความปลอดภัยมากขึ้น โดยเน้นไปที่องค์กร หน่วยงาน หรือนิติบุคคลให้มี “มาตรฐาน” ในการจัดการข้อมูลส่วนบุคคลอย่างเหมาะสมและเพียงพอ เมื่อมีความจำเป็นต้องขอใช้ข้อมูลส่วนบุคคล ทั้งนี้ก็เพื่อป้องกันความเสี่ยงที่จะมีผลกระทบไปถึงการรักษาความลับ (Confidentiality) ความถูกต้องสมบูรณ์ (Integrity) และความพร้อมใช้งาน (Availability) ของข้อมูลส่วนบุคคล ที่ก่อให้เกิดแนวโน้มให้เกิดผลกระทบเชิงลบหรือความเสียหายในระดับบุคคลหรือองค์กร พ.ร.บ. นี้มีผลบังคับใช้แล้วในเดือนพฤษภาคม 2563 สามารถอ่านฉบับเต็มได้ที่เว็บไซต์ทางการนี้</p>
