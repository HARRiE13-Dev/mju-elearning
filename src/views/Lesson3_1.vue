                การเลือกใช้สีที่เหมาะสม
                จะสามารถโน้มน้าวและสร้างแรงจูงใจในการอ่านได้ โดยสีโทนเย็น (Cool
                colors) เช่น สีฟ้าและสีเขียว จะเหมาะสำหรับทำเป็นพื้นหลัง
                (Background) และสีโทนอบอุ่น (Warm colors) เช่น สีส้มและสีแดง
                ไว้สำหรับเป็นสีของข้อความ (Text)
                การใช้พื้นหลังและการจัดวางองค์ประกอบต้องให้เหมาะสมกับงาน
                ซึ่งผู้ใช้สามารถเลือกจากสไลด์ต้นแบบ (Template) และ ธีม (Theme)
                ที่มีให้ในโปรแกรม
                หรือสร้างขึ้นใช้เองโดยเฉพาะเพื่อให้เป็นเอกลักษณ์และไม่ซ้ำกับของคนอื่น
              </p>
              <h6 class="rem2">3.4.1.1 การใช้สไลด์ต้นแบบ (Template)</h6>
              <p class="rem5">
                การสร้างงานนำเสนอใหม่ด้วย Microsoft PowerPoint
                สามารถเริ่มต้นโดยการใช้สไลด์ต้นแบบ (Template) และชุดของสี
                ฟอนต์และเอฟเฟ็กต์ หรือที่เรียกว่า ธีม (Theme) ที่มีในโปรแกรม
                โดยเมื่อเปิดโปรแกรม PowerPoint ขึ้นมา จะมี Theme
                สำเร็จรูปให้เลือ
              </p>
              <p class="rem5">
                หากไม่มี Theme ที่ถูกใจ ผู้ใช้สามารถ search โดยพิมพ์ keyword
                ลงไปแล้วกด Enter หรือคลิกที่หมวดหมู่ที่อยู่ด้านล่างกล่อง search
                เช่น Presentation, Theme, Education
                จะมีสไลด์ต้นแบบขึ้นมาให้เลือกมากมาย
                (แต่ต้องมีการเชื่อมต่อกับอินเทอร์เน็ต)
                เมื่อเลือกแล้วผู้ใช้สามารถปรับแก้ข้อความ/ใส่ข้อความใหม่
                ย้ายรูปภาพ/ใส่รูปภาพใหม่ได้ตามต้องการ
              </p>
              <br />
              <div class="text-center font-italic">
                <img src="/img/lesson3/1.png" width="80%" />
                <p>
                  <br />ภาพที่ 1 – Theme สำเร็จรูปที่มีอยู่ในโปรแกรม Microsoft
                  PowerPoint
                </p>
              </div>
              <br />
              <h6 class="rem2">3.4.1.2 การสร้าง Theme ใหม่</h6>
              <p class="rem5">
                ผู้ใช้สามารถสร้าง Theme ของตัวเองได้ หาก Theme
                สำเร็จรูปที่มีอยู่ไม่ตรงกับความต้องการ โดยเริ่มจาก File ---> New
                แล้วเลือก Blank Presentation จากนั้นคลิกเม้าส์ขวาที่สไลด์
                แล้วเลือก Format Background จากนั้นไปที่ Fill ติกที่ช่อง Picture
                or texture fill และ คลิกที่ Insert
                แล้วไปเลือกไฟล์ภาพที่เตรียมไว้เพื่อทำเป็นพื้นหลัง แล้วคลิกที่
                Apply to All
              </p>
              <br />
              <div class="text-center font-italic">
                <img src="/img/lesson3/2.png" width="80%" />
                <p><br />ภาพที่ 2 – ขั้นตอนการสร้าง Theme ใหม่</p>
              </div>
              <br />
              <p class="rem5">
                เมื่อสร้างเสร็จแล้วเราสามารถ Save ไว้เป็น Theme
                ของเราที่จะใช้งาน โดยไปที่ Design บน Menu Bar
                จากนั้นคลิกที่รูปสามเหลี่ยมด้านล่าง เลือกที่ Save Current Theme
                แล้วตั้งชื่อ Theme เพื่อไว้ใช้งานต่อไป
              </p>
              <br />
              <div class="text-center font-italic">
                <img src="/img/lesson3/3.png" width="80%" />
                <p><br />ภาพที่ 3 – การบันทึก Theme ใหม่สำหรับการนำไปใช้งาน</p>
              </div>
              <br />
              <p class="rem5 font-italic font-weight-bold">
                *** ข้อควรระวัง การสร้างธีมไม่ควรใช้สีมากจนเกินไป ควรใช้ไม่เกิน
                3-4 สี เนื่องจากหากใช้หลายสีจะเป็นการรบกวนสายตา ทำให้งานอ่านยาก
                ควรใช้ Background เป็นสีพื้นเรียบ เช่น สีขาว เทา ดำ หรือสีพื้น
                จะทำให้ในส่วนของเนื้อหา และรูปภาพดูเด่นขึ้น ***
              </p>
              <br />
              <h6 class="rem2">
                3.4.1.3 การใช้ Slide Master เพื่อสร้างต้นแบบสไลด์
              </h6>
              <p class="rem5">
                การสร้าง presentation
                นอกจากการใช้ธีมนำเสนอสำเร็จรูปหรือการสร้างธีมให้ตรงใจขึ้นใช้เองแล้ว
                ผู้ใช้ยังสามารถสร้างต้นแบบสไลด์ตามความต้องการได้ โดยการออกแบบ
                Slide Master หรือ โครงสร้างของสไลด์ตามความต้องการ
                ซึ่งเป็นการกำหนดหรือใส่ส่วนประกอบต่างๆ ของสไลด์
                เพื่อให้สไลด์มีรูปแบบเดียวกัน ทั้ง สี เส้น รูปทรง ข้อความ รูปภาพ
                โดยผู้ใช้สามารถแบ่งการนำเสนอเป็นส่วนๆ
                และกำหนดรูปแบบของสไลด์ในแต่ละส่วนให้แตกต่างกันได้ (เช่น
                สไลด์แรกจะเป็นสไลด์หน้าปก และสไลด์ถัดไปแบ่งเป็นบทๆ
                โดยแต่ละบทมีรูปแบบของสไลด์แตกต่างกัน) การใช้ Slide Master
                จะทำให้งานนำเสนอของเรามีสไตล์และสามารถทำได้ในเวลาที่รวดเร็ว
              </p>
              <br />

              <p class="font-weight-bold rem2">วิธีการสร้างต้นแบบสไลด์</p>
              <p class="rem5">
                1) เปิดโปรแกรม PowerPoint แล้วไปที่ File -------> New เลือก
                Blank Presentation
              </p>
              <p class="rem5">2) คลิกที่แถบ View -------> Slide Master</p>
