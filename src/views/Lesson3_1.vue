                <p>
                  <br />ภาพที่ 7 ตัวอย่างการจัดวาง layout
                  การกำหนดฟอนต์ที่ต้องการไว้บนสไลด์ต้นแบบ
                </p>
              </div>
              <br />
              <p class="rem5">
                4) หลังจากที่ทำการแก้ไขสไลด์ตามที่ต้องการเรียบร้อยแล้ว
                ให้คลิกที่แถบ Slide Master แล้วคลิกที่ Close Master View
                หรือคลิกที่แถบ View -------> Normal เพื่อกลับไปที่หน้าปกติ
              </p>
              <p class="rem5">
                5) การเรียกใช้สไลด์ต้นแบบที่ทำ layout ไว้ในชุดของ Slide Master
                ให้คลิกที่สไลด์ที่จะกำหนดรูปแบบให้ จากนั้นคลิกที่แถบ Home
                แล้วคลิกที่ ที่ด้านข้างไอคอน Slide Layout ดังภาพที่ 8
                และจะปรากฏชุดสไลด์ต้นแบบที่อยู่ใน Slide Master
                มาให้เลือกใช้ตามต้องการ (ดังภาพที่ 9)
