              <h4>• ประโยชน์ของระบบฐานข้อมูล</h4>
              <p class="rem2 text-justify">
                1. สามารถลดความซ้ำซ้อนของข้อมูลได้ (Reducing Data Redundancy)
                การจัดเก็บข้อมูลในรูปแบบแฟ้มข้อมูล
                ข้อมูลบางชุดที่เหมือนกันจะถูกจัดเก็บไว้หลายแห่งทำให้เกิดความซ้ำซ้อน
                แต่ในระบบฐานข้อมูลจะรวบรวมไว้ในที่เดียวกัน ทำให้หน่วยงานต่างๆ
                สามารถที่จะเรียกใช้แฟ้มข้อมูลเดียวกันได้และลดความซ้ำซ้อนลงได้
              </p>
              <p class="rem2 text-justify">
                2. ข้อมูลมีความสอดคล้องและถูกต้องมากขึ้น (Data Consistency and
                Integrity)
                ในระบบแฟ้มข้อมูลมีการจัดเก็บข้อมูลชุดเดียวกันแยกอิสระกันหลายแห่ง
                เมื่อมีการปรับปรุงข้อมูลจะต้องแก้ไขหลายแห่ง
                ซึ่งอาจทำให้ค่าข้อมูลไม่สอดคล้องกัน มีความขัดแย้งกัน
                และมีค่าไม่ถูกต้องไม่สามารถนำไปใช้งานได้
                แต่ในระบบฐานข้อมูล ข้อมูลถูกจัดเก็บไว้ในที่แห่งเดียวกัน
                จะมีการออกแบบรูปแบบมาตรฐานเดียวกัน หากมีการแก้ไขข้อมูลใดๆ
                ก็จะปรับปรุงข้อมูล ณ แห่งเดียว
                ทำให้ข้อมูลเหมือนกันหมดและมีความถูกต้องมากยิ่งขึ้น
              </p>
              <p class="rem2 text-justify">
                3. สามารถใช้ข้อมูลร่วมกันได้ ( Sharing of Data)
                ระบบฐานข้อมูลจะเป็นการจัดเก็บข้อมูลรวมไว้ด้วยกัน
                ดังนั้นหากผู้ใช้ต้องการใช้ข้อมูลในฐานข้อมูลที่มาจากแฟ้มข้อมูลต่างๆ
                ก็จะทำได้โดยง่ายสะดวก เช่น
                ข้อมูลนักศึกษาและการลงทะเบียนจากฝ่ายทะเบียนสามารถให้ฝ่ายงานกองทุนกู้ยืมนำข้อมูลนักศึกษาไปใช้งานได้
              </p>
              <p class="rem2 text-justify">
                4. สามารถกำหนดให้ข้อมูลมีรูปแบบที่เป็นมาตรฐานเดียวกันได้
                (Standard of Data Structure)
                ในการจัดเก็บข้อมูลให้เป็นลักษณะเดียวกัน เช่น โครงสร้างข้อมูล
                ประเภทของข้อมูล เป็นต้น
                ทำให้สามารถกำหนดมาตรฐานของข้อมูลได้รวมทั้งมาตรฐานต่างๆ
                ในการจัดเก็บข้อมูลให้เป็นไปในลักษณะเดียวกันได้
                เช่นการกำหนดรูปแบบการเขียนวันที่ ในลักษณะ วัน/เดือน/ปี หรือ
                ปี/เดือน/วัน
              </p>
              <p class="rem2 text-justify">
                5. สามารถกำหนดระบบความปลอดภัยของข้อมูลได้ (Data Security and
                Privacy)
                ถึงแม้ว่าในระบบฐานข้อมูลจะสามารถให้มีการใช้ข้อมูลร่วมกันหลายหน่วยงาน
                แต่การเข้าถึงข้อมูลในระบบฐานข้อมูลจะมีการกำหนดสิทธิการเข้าใช้
                โดยสามารถการกำหนดว่าผู้ใดมีสิทธิสามารถเข้าใช้งานระบบได้บ้าง
                และสามารถใช้ข้อมูลในส่วนใดได้บ้าง มากน้อยแค่ไหน เช่น
                นักศึกษาสามารถเรียกดูข้อมูลเกรดที่ได้รับในแต่ละรายวิชา
                แต่ไม่สามารถทำการแก้ไขข้อมูลได้ เป็นต้น
              </p>
              <p class="rem2 text-justify">
                6. เกิดความเป็นอิสระของข้อมูล (Data Independency)
                ในระบบฐานข้อมูลโปรแกรมประยุกต์ที่สร้างขึ้นจะไม่ขึ้นกับโครงสร้างของการจัดเก็บข้อมูล ข้อมูล
                ดังนั้นหากมีการเปลี่ยนแปลงโครงสร้างของตารางการจัดเก็บข้อมูลในฐานข้อมูล
                ก็ไม่จำเป็นต้องแก้ไขโปรแกรมประยุกต์ที่ใช้
                จึงเกิดความเป็นอิสระระหว่างการจัดเก็บข้อมูลและการประยุกต์ใช้
              </p>
              <p class="rem2 text-justify">
                7. การสำรองและกู้คืนข้อมูล (Backup and Recovery)
                ระบบโปรแกรมจัดการฐานข้อมูล (DBMS)
                สามารถช่วยผู้ใช้ในการจัดการสำรองข้อมูลได้อัตโนมัติ
                และสามารถกู้คืนข้อมูลนำกลับมาใช้ใหม่ได้เมื่อระบบเกิดความล้มเหลว
              </p>
              <br />
              <h5 class="rem2">
                การป้องกันข้อมูลในระบบคอมพิวเตอร์และความปลอดภัยอาชญากรรมทางไซเบอร์
                พรบ.
              </h5>
              <p class="rem5 text-justify">
                การขยายตัวขององค์กรและการเปลี่ยนแปลงของเทคโนโลยีคอมพิวเตอร์ที่ทันสมัยทำให้การแลกเปลี่ยนข้อมูลข่าวสาร
                การเข้าถึงข้อมูลได้ง่ายส่งผลให้มีการใช้งานเทคโนโลยีจากผู้ใช้ทั่วทุกมุมโลก
                อย่างไรก็ตามแม้ว่าเทคโนโลยีคอมพิวเตอร์และระบบเครือข่ายอินเทอร์เน็ตจะมีผลดีและมีประโยชน์มากเพียงไรก็ตาม
                แต่อีกด้านหนึ่งแล้วคอมพิวเตอร์สามารถเป็นภัยได้เช่นกันหากผู้ใช้ไม่ระมัดระวังหรือนำไปใช้ในทางที่ไม่ถูกต้อง
                และยังสามารถเป็นช่องทางให้มิจฉาชีพเข้ามาแสวงหาประโยชน์อย่างผิดกฎหมาย
                หรือทำให้เกิดปัญหาการขยายตัวของอาชญากรรมข้ามชาติ
                รวมถึงอาชญากรรมรูปแบบใหม่ เช่น อาชญากรรมคอมพิวเตอร์ (Computer
                Crime) หรือ อาชญากรรมไซเบอร์ (Cyber Crime)
                ซึ่งในปัจจุบันถือเป็นปัญหาทางสังคมอย่างหนึ่งที่กำลังเพิ่มความรุนแรงและสร้างความเสียหายแก่สังคม
                ด้วยเหตุนี้การรักษาความปลอดภัยสำหรับระบบสารสนเทศนั้นมีบทบาทความสำคัญมากขึ้นเป็นลำดับ
              </p>
              <br />
              <h4>• ความมั่นคงปลอดภัยสำหรับระบบสารสนเทศ</h4>
              <p class="rem2 text-justify">
                ความมั่นคงปลอดภัยของระบบสารสนเทศ (Information System Security)
                คือการป้องกันข้อมูลสารสนเทศรวมถึง
                องค์ประกอบอื่นๆ ที่เกี่ยวข้อง
                เช่น
                ระบบและฮาร์ดแวร์ที่ใช้ในการจัดเก็บและถ่ายโอนข้อมูลสารสนเทศนั้นให้รอดพ้นจากอันตรายอยู่ในสถานะที่มีความปลอดภัยไร้ความกังวลและความกลัว
              </p>
              <p class="rem2 font-weight-bold">
                ความไม่ปลอดภัยของระบบสารสนเทศสามารถเกิดขึ้นได้ 2 ลักษณะคือ
              </p>
              <p class="rem2 text-justify">
                1. เกิดจากความตั้งใจของบุคคล เช่น
                ผู้ประสงค์ร้ายที่เข้ามาขโมยความลับจากระบบสารสนเทศ
                หรือจากการปล่อยโปรแกรมไวรัสเข้ามาทำลายข้อมูลในระบบ
                การลักลอบขโมยข้อมูลจากแฮกเกอร์ (Hacker) การขโมยรหัส
                การขโมยโปรแกรม การใช้ระบบคอมพิวเตอร์เพื่อขโมยเงินหรือสิ่งของ
                การเปลี่ยนแปลงข้อมูลโดยไม่อนุญาต การโจรกรรมระบบ เป็นต้น
              </p>
              <p class="rem2 text-justify">
                2. เกิดจากความไม่ตั้งใจของบุคคล หรือจากอุบัติเหตุ เช่น
                ภัยธรรมชาติ การจลาจลหรือสงคราม การลบข้อมูลจากระบบโดยไม่ตั้งใจ
                การลืมรหัสผ่านไว้ทำให้ผู้ประสงค์ดีขโมยไป
                ความผิดพลาดของฮาร์ดแวร์และซอฟต์แวร์ ความประมาทของผู้ใช้
                กำลังไฟตก ไม่ได้ป้องกันไวรัส
                ข้อมูลถูกทำลายเนื่องจากความผิดปกติของเครื่องคอมพิวเตอร์และโปรแกรม
                เป็นต้น
              </p>
              <br />
              <h4>• ภัยคุกคามต่อระบบคอมพิวเตอร์</h4>
              <h5 class="rem2">
                1. ภัยคุกคามทางระบบฮาร์ดแวร์ (Hardware Security Threats)
              </h5>
              <p class="rem5">1.1 ภัยที่มีต่อระบบการจ่ายไฟฟ้า</p>
              <p class="rem5">
                1.2 ภัยที่เกิดจากการทําลายทางกายภาพโดยตรง ต่อระบบคอมพิวเตอร์นั้น
                ๆ
              </p>
              <p class="rem5">1.3 ภัยจากการลักขโมยโดยตรง</p>
              <h5 class="rem2">
                2. ภัยคุกคามทางระบบซอฟต์แวร์ (Software Security Threats)
              </h5>
              <p class="rem5">
                2.1 การลบซอฟต์แวร์หรือการลบเพียงบางส่วน ของซอฟต์แวร์นั้น ๆ
              </p>
              <p class="rem5">2.2 การขโมยซอฟต์แวร์ (Software Theft)</p>
              <p class="rem5">
                2.3 การเปลี่ยนแปลงแก้ไขซอฟต์แวร์ (Software Modification)
              </p>
              <p class="rem5">2.4 การขโมยข้อมูล (Information Leaks)</p>
              <h5 class="rem2">
                3. ภัยคุกคามที่มีต่อระบบข้อมูล (Data Threats)
              </h5>
              <p class="rem5">3.1 การที่ข้อมูลอาจถูกเปิดเผยโดยมิได้รับอนุญาต</p>
              <p class="rem5">
                3.2
                การที่ข้อมูลอาจถูกเปลี่ยนแปลงแก้ไขเพื่อผลประโยชน์โดยมิได้มีการตรวจสอบแก้ไข
              </p>
              <p class="rem5">
                3.3 การที่ข้อมูลนั้นถูกทำให้ไม่สามารถนํามาใช้งานได้
              </p>
              <h5 class="rem2">4. ภัยคุกคามทางไซเบอร์ (Cyber Threats)</h5>
              <p class="rem5 text-justify">
                ภัยคุกคามทางไซเบอร์หมายความว่า การกระทำหรือการดำเนินการใดๆ
                โดยมิชอบโดยใช้คอมพิวเตอร์หรือระบบคอมพิวเตอร์
                หรือโปรแกรมไม่พึงประสงค์โดยมุ่งหมายให้เกิดการประทุษร้ายต่อระบบคอมพิวเตอร์
                ข้อมูลคอมพิวเตอร์ หรือข้อมูลอื่นที่เกี่ยวข้อง
                และเป็นภยันตรายที่ใกล้จะถึงที่จะก่อให้เกิดความเสียหายหรือส่งผลกระทบต่อการทำงานของคอมพิวเตอร์
                ระบบคอมพิวเตอร์ หรือข้อมูลอื่นที่เกี่ยวข้อง
              </p>
              <p class="rem5 text-justify">
                4.1 ภัยคุกคามทางไซเบอร์ในระดับไม่ร้ายแรง คือ
                เหตุการณ์ที่เป็นภัยคุกคามมีผลทำให้ประสิทธิภาพ ระบบเครือข่าย
                หรือคอมพิวเตอร์ทำให้ช้าลง
              </p>
              <p class="rem5 text-justify">
                4.2 ภัยคุกคามทางไซเบอร์ในระดับร้ายแรง
                คือภัยคุกคามที่โจมตีระบบคอมพิวเตอร์โดยกระทบต่อความมั่นคงของรัฐทั้งด้านความสัมพันธ์ระหว่างประเทศ
                การป้องกันประเทศ เศรษฐกิจ ความปลอดภัยสาธารณะ
              </p>
              <p class="rem5 text-justify">
                4.3 ภัยคุกคามทางไซเบอร์ในระดับวิกฤติ
                คือภัยคุมคามที่มีลักษณะการโจมตีระบบคอมพิวเตอร์ที่ส่งผลกระทบรุนแรงต่อโครงสร้างพื้นฐานของระบบสารสนเทศภายในประเทศเป็นบริเวณกว้าง
                และการโจมตีที่เป็นการก่ออาชญากรรมทางไซเบอร์
              </p>
              <br />
              <h4>• อาชญากรรมทางคอมพิวเตอร์ (Computer Crime)</h4>
              <p class="rem2 text-justify">
                อาชญากรรมทางคอมพิวเตอร์ (Computer Crime ) คือ
                การกระทำที่ผิดกฎหมายโดยใช้เทคโนโลยีสารสนเทศหรือคอมพิวเตอร์โดยไม่ได้รับอนุญาต
                หรือนำไปใช้เป็นเครื่องมือเพื่อกระทำการที่ผิดกฎหมายก่อให้เกิดความเสียหายแก่ระบบคอมพิวเตอร์และข้อมูลของผู้อื่น
                ในขณะเดียวกันก็ทำให้ผู้กระทำความผิดได้รับประโยชน์ เช่น ทำลาย
                เปลี่ยนแปลง หรือขโมยข้อมูลต่างๆ เป็นต้น
              </p>
              <p class="rem2 text-justify">
                สำหรับอาชญากรรมในระบบเครือข่ายคอมพิวเตอร์
                อาจเรียกได้อีกอย่างหนึ่ง คือ อาชญากรรมไซเบอร์ (Cybercrime) หรือ
                อาชญากรรมอินเทอร์เน็ต (Internet Crime)
                ในปัจจุบันอาชญากรที่ก่ออาชญากรรมประเภทนี้ มักถูกเรียกว่า
                แครกเกอร์ (Cracker) ซึ่ง แครกเกอร์ (Cracker) คือ แฮกเกอร์
                (Hacker)
                ที่ลักลอบเข้าไปยังคอมพิวเตอร์ของผู้อื่นเพื่อวัตถุประสงค์ในเชิงธุรกิจ
                นอกจากนี้ยังมีคำว่า Hacktivist หรือ cyber terrorist ได้แก่
                แฮกเกอร์ที่ใช้อินเทอร์เน็ตในการส่งข้อความเพื่อผลประโยชน์ทางการเมืองไปยังบุคคลอื่น
                ๆ
              </p>
              <p class="rem2">
                แฮกเกอร์ (Hacker) คือ
                บุคคลที่ใช้ความรู้ความสามารถในทางที่ไม่ถูกต้องหรือผิดกฎหมาย
                ขาดจริยธรรม
                ในการลักลอบเข้าไปทำลายระบบคอมพิวเตอร์และหาประโยชน์ให้กับตนเอง
                แฮกเกอร์สามารถแบ่งออกเป็น 2 ประเภท คือ
              </p>
              <p class="rem5">
                1. white hat hacker คือ แฮกเกอร์ที่มีจริยธรรมที่คอยดู แก้ไข
                ความผิดพลาดของข้อมูลของเรา เพื่อแจ้งให้เราทราบว่า
                คอมพิวเตอร์ของเราเกิดความเสียหายอะไรบ้าง
              </p>
              <p class="rem5">
                2. Black hat hacker คือ มีความรู้เหมือนกันแต่ไม่มีจริยธรรม
                ชอบลักลอบข้อมูลไปโดยที่เจ้าตัวไม่รู้ว่า ได้ถูกลักลอบข้อมูล
              </p>

