              <p class="rem2 text-justify">
                1. การโจรกรรมทางคอมพิวเตอร์ (Computer spionage)
                เมื่อระบบคอมพิวเตอร์ได้กลายเป็นอุปกรณ์หลักในการเก็บบันทึกข้อมูล
                จึงทำให้ผู้กระทำผิดมีการใช้เครื่องมือพิเศษเพื่อดักรับข้อมูลในระหว่างการติดต่อสื่อสาร
                ข้อมูลส่วนใหญ่ที่ตกเป็นเป้าหมายของผู้กระทําในรูปแบบนี้ได้แก่
                โปรแกรมคอมพิวเตอร์, ข้อมูลการศึกษาวิจัย, ความลับทางการทหาร,
                ข้อมูลการประกอบธุรกิจประเภทบัญชีการเงิน
                รวมทั้งข้อมูลเกี่ยวกับลูกค้า
                โดยในระยะหลังที่ผ่านมาการกระทําความผิดรูปแบบนี้ก็มักเกิดขึ้นกับข้อมูลทางธุรกิจเป็นส่วนใหญ่
                ซึ่งคู่กรณีมักเป็นบริษัทคู่แข่งกัน
              </p>
              <p class="rem2 text-justify">
                2. การเจาะระบบ หรือเข้าถึงระบบของผู้อื่นโดยไม่ได้รับอนุญาต
                (Computer hacking)
                ผู้กระทำผิดจะทำการเข้าแทรกแซงข้อมูลและลักลอบใช้ข้อมูลเพื่อแสวงหาผลประโยชน์ในทางมิชอบ
                รวมถึงการขโมยข้อมูลทางอินเทอร์เน็ตเพื่อลักลอบเข้าใช้บริการสารสนเทศ
                เช่น
                การขโมยรหัสผ่านจากผู้ให้บริการอินเทอร์เน็ตหรือเว็บไซต์เพื่อลักลอบเข้าใช้บริการโทรศัพท์
                อินเทอร์เน็ต
                รวมถึงข้อมูลส่วนบุคคลที่ผูกติดอยู่กับเทคโนโลยีโดยเฉพาะข้อมูลทางด้านการเงิน
                การเจาะผ่านระบบอินเทอร์เน็ตเข้าไปแล้วแอบล้วงความลับทางการค้า
                การดักฟังข้อมูลจากระบบสนทนา (Chat messenger)
                การเปลี่ยนแปลงข้อมูลคอมพิวเตอร์
                เพื่อนำมาเป็นประโยชน์ต่อกิจการของตน เป็นต้น
              </p>
              <p class="rem2 text-justify">
                3. การขโมย การละเมิดลิขสิทธิ์ ดัดแปลง การปลอมแปลง
                การคัดลอกผลงานที่มีลิขสิทธิ์
                ในปัจจุบันคอมพิวเตอร์ส่วนบุคคลและอินเทอร์เน็ตถูกใช้เป็นสื่อในการก่ออาชญากรรม
                โดยการโจรกรรมทางออนไลน์หมายรวมถึง การละเมิดลิขสิทธิ์ ใดๆ
                ที่เกี่ยวข้องกับการใช้อินเทอร์เน็ตเพื่อจำหน่ายหรือเผยแพร่ผลงานสร้างสรรค์ที่ได้รับการคุ้มครองลิขสิทธิ์
                เช่น การละเมิดลิขสิทธิ์ซอฟต์แวร์
                การลักลอบคัดลอกผลงานผู้อื่นโดยไม่ได้รับอนุญาตเพื่อนำมาใช้หรือจำหน่ายต่อ
                การปลอมแปลงสื่อมัลติมีเดียที่มีลิขสิทธิ์ต่างๆ เช่น หนัง เพลง
                การลักลอบใช้โปรแกรมคอมพิวเตอร์โดยไม่ได้รับอนุญาต
                การละเมิดทรัพย์สินทางปัญญาด้วยการดาวน์โหลด
                หรือทําซ้ำผลงานอันมีลิขสิทธิ์ต่างๆ เช่น เป็นต้น
              </p>
              <p class="rem2 text-justify">
                4.
                การฟอกเงินทางอิเล็กทรอนิกส์ซึ่งใช้อุปกรณ์ทางคอมพิวเตอร์และการสื่อสารเป็นเครื่องมือทำให้สามารถกลบเกลื่อนอำพรางตัวตนของผู้กระทำความผิดได้ง่ายขึ้นและให้มาเป็นทรัพย์สินที่ถูกกฎหมายเช่น
                การปลอมแปลงเอกสารข้อมูลลูกค้าของพนักงานธนาคารเพื่อใช้ในการเบิกถอนเงินจากบัญชีลูกค้าโดยไม่ได้รับอนุญาต
                การปันหุ้น การทำธุรกิจสินค้าหนีภาษี การปลอมแปลงเงินตรา เป็นต้น
              </p>
              <p class="rem2 text-justify">
                5. การค้าขายหรือชวนลงทุนโดยหลอกลวงผ่านอินเทอร์เน็ต เช่น
                การส่งอีเมลที่เป็นสแปมในการเชิญชวนให้เข้าใช้บริการฟรี
                หรือส่งข้อความโฆษณาชวนเชื่อในการให้ส่วนลดพิเศษเพื่อเข้าใช้บริการ
                แต่ไม่มีบริการเหล่านั้นจริง
                รวมถึงการส่งอีเมลและเอกสารปลอมในการแอบอ้างหาผลประโยชน์ต่าง ๆ
              </p>
              <p class="rem2 text-justify">
                6. การก่อกวนทำลายระบบคอมพิวเตอร์หรือก่อวินาศกรรมระบบ (Computer
                sabotage) และการข่มขู่ทางอินเทอร์เน็ต (Computer erpressung)
                ความผิดส่วนใหญ่ที่เกิดขึ้นมักเป็นการกระทำต่อคอมพิวเตอร์ส่วนบุคคล
                ด้วยวิธีการปล่อยโปรแกรมไวรัส (Virus) หรือ วอร์ม (Worm)
                ที่มีเป้าหมายทำลายระบบคอมพิวเตอร์และข้อมูลของผู้อื่น
                การทำลายระบบรักษาความปลอดภัย
                ส่วนการข่มขู่ทางอินเทอร์เน็ตมักกระทำผ่านทางจดหมายอิเล็กทรอนิกส์
                ผู้เสียหายจะถูกข่มขู่ให้ต้องยินยอมกระทำการอย่างหนึ่งอย่างใด เช่น
                การรีดไถเงิน มิเช่นนั้นระบบคอมพิวเตอร์ของเขาจะถูกทําลาย
                หรือทำให้เสียหายจนใช้ประโยชน์ไม่ได้
                หรือบางกรณีผู้กระทำผิดจะใช้วิธีเขียนโปรแกรมเข้ารหัสเครื่องคอมพิวเตอร์ของเหยื่อ
                เพื่อไม่ให้เหยื่อเข้าไปใช้งานได้จากนั้นจึงข่มขู่ให้เหยื่อจ่ายเงินเพื่อแลกกับการถอดรหัสดังกล่าว
                เป็นต้น
              </p>
              <p class="rem2 text-justify">
                7. การเผยแพร่ภาพ เสียง ข้อมูลที่ไม่เหมาะสม หรือ ข้อมูลเท็จ
                ผ่านทางสื่อออนไลน์ และได้กลายเป็นอาชญากรรมไซเบอร์
                สามารถสร้างความเสียหายต่อประโยชน์สาธารณะหรือส่งผลกระทบต่อ
                ค่านิยม แนวคิด สังคม รวมทั้งพัฒนาการของเด็กและเยาวชน เช่น
                จดหมายลูกโซ่ ภาพอนาจารทางออนไลน์ การสร้างข่าวปลอม
                ภาพอาวุธปืนอาวุธสงคราม หรือข้อความที่มีเนื้อหาเป็นภัยต่อสังคม
                หรือความมั่นคงทางเครือข่าย การพนันบนเครือข่าย
                การหมิ่นประมาทผู้อื่นโดยการโฆษณา โดยอาศัยเครือข่ายอินเทอร์เน็ต
                หรือ เผยแพร่ข้อมูลที่มีเนื้อหาแอบแฝงแนวคิดก้าวร้าว รุนแรง หรือ
                แนวคิดในการดูหมิ่นชนชาติอื่น
                การเลือกปฏิบัติรวมทั้งการหมิ่นประมาทผ่านสื่อบริการทั้งหลายบนเครือข่ายอินเตอร์เน็ต
                โดยเฉพาะอย่างยิ่งในเว็บไซต์ กระดานข่าว
                จดหมายอิเล็กทรอนิกส์และห้องสนทนาออนไลน์ เป็นต้น
              </p>
              <p class="rem2 text-justify">
                8. การใช้เทคโนโลยีคอมพิวเตอร์ดัดแปลงข้อมูลบัญชีธนาคาร
                หรือการโอนเงินจากบัญชีหนึ่ง เข้าไปอีกบัญชีหนึ่ง
                โดยที่ไม่มีการเปลี่ยนถ่ายทรัพย์สินกันจริง
                การบิดเบือนข้อมูลการชำระเงิน
                การลักลอบเปลี่ยนแปลงข้อมูลทางบัญชีรายรับ-รายจ่ายของบริษัท
              </p>
              <br />
              <h5>
                แนวทางการป้องกันและแก้ไขปัญหาอาชญากรรมทางคอมพิวเตอร์ที่สามารถปฏิบัติได้ดังนี้
              </h5>
              <p class="rem2 text-justify">
                องค์กรสามารถป้องกันอาชญากรรมคอมพิวเตอร์ได้โดยการติดตั้งซอฟแวร์และฮารด์แวร์
                หรือ ระบบรักษาความมั่นคงปลอดภัย
                และจะต้องมีการประยุกต์ใช้ทั้งนโยบายและกระบวนการจัดการ
                โดยเฉพาะการจัดการด้านบุคลากรจึงจะสามารถป้องกันการโจมตีได้อย่างมีประสิทธิภาพ
              </p>
              <p class="rem2">
                1.
                ควรมีการวางแนวทางและกฎเกณฑ์ในการรวบรวมพยานหลักฐานและดำเนินคดีอาญากรรมคอมพิวเตอร์
              </p>
              <p class="rem2">
                2. ส่งเสริมจริยธรรมในการใช้คอมพิวเตอร์
                โดยการสร้างความรู้ความเข้าใจแก่บุคคลทั่วไปในการใช้คอมพิวเตอร์
                อย่างถูกต้อง โดยการปลูกฝังเด็กตั้งแต่ในวัยเรียนให้เข้าในกฎเกณฑ์
                มารยาทในการใช้คอมพิวเตอร์ อย่างถูกวิธี และเหมาะสม
              </p>
              <p class="rem2">
                3. เผยแพร่ความรู้เรื่องอาชญากรรมคอมพิวเตอร์แก่ผู้ใช้คอมพิวเตอร์
                หน่วยงาน และองค์กรต่างๆ ให้เข้าใจ แนวคิดและวิธีการ
                ของอาชญากรรมคอมพิวเตอร์ เพื่อป้องกันตนเองเป็นเบื้องต้น
              </p>
              <p class="rem2">
                4. บัญญัติกฎหมายเฉพาะเกี่ยวกับอาชญากรรมคอมพิวเตอร์
                หรือแก้ไขเพิ่มเติมกฎหมายที่มีอยู่ในครอบคลุมการ
                กระทำอันเป็นความผิดเกี่ยวกับอาชญากรรมคอมพิวเตอร์ทุกประเภท
              </p>
              <p class="rem2">
                5. จัดตั้งหน่วยงานเกี่ยวกับอาชญากรรมคอมพิวเตอร์
                และคณะทำงานที่มีความรู้ความชำนาญในคดีอาชญากรรม คอมพิวเตอร์
              </p>
              <p class="rem2">
                6. การตรวจสอบระบบคอมพิวเตอร์และป้องกันการทำงานที่ผิดพลาดบ่อยๆ
                รวมถึงการติดตั้งโปรแกรมไวรัส
              </p>
              <p class="rem2">
                7. การควบคุมการเข้าใช้ระบบสารสนเทศของผู้ใช้ เช่น
                การจัดสรรสิทธิในการเข้าใช้ข้อมูล และมีความเข้มงวดใน
                การอนุญาตเข้าใช้คอมพิวเตอร์หรือระบบสารสนเทศ
              </p>

