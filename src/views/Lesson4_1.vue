                เกิดแก๊งหรือกลุ่มมิจฉาชีพที่ใช้สื่อออนไลน์เพื่อล่อลวงหรือต้มตุ๋นผู้อื่น
                (scammer)
                รวมถึงคนที่อาจจะใช้สื่อออนไลน์เพื่อสร้างความรุนแรงเช่นการด่าทอหรือการรังแกผู้อื่น
                สื่อลามกอานาจาร ยาเสพติดหรือการพนัน
                แม้จะมีมาตรการทางกฎหมายเข้ามาดูแลเช่น
                พระราชบัญญัติว่าด้วยการกระทําความผิดเกี่ยวกับคอมพิวเตอร์ พ.ศ.
                ๒๕๖๐ แต่ก็ยากจะควบคุมได้ทั้งหมด ดังนั้น
                ผู้ใช้เองก็ต้องมีภูมิรู้และมีการรู้เท่าทันเพื่อจะได้ป้องกันตนเองจากบุคคลที่อาจจะแฝงตัวผ่านโลกออนไลน์เพื่อนำข้อมูลหรือการสื่อสารนั้นๆ
                ไปใช้ประโยชน์ในทางมิชอบอีกด้วย
              </p>
              <p class="rem5">
                • เราสามารถสื่อสารผ่านอุปรณ์ใดๆ ก็ได้ (Any device)
                เทคโนโลยีดิจิทัลได้สร้างโลกใบใหม่ที่ชื่อว่าโลกออนไลน์
                การเข้าถึงข้อมูลข่าวสารและการติดต่อสื่อสารไม่ได้จำกัดแค่วิทยุ
                โทรทัศน์ อีกต่อไป แต่เราสามารถสื่อสารผ่านอุปกรณ์ต่างๆ
                ที่เชื่อมต่อกับระบบอินเทอร์เน็ต เช่น โทรศัพท์มือถือ โทรทัศน์
                คอมพิวเตอร์ เครื่องเล่นเกม โดยสามารถใช้ในการติดต่อสื่อสาร
                หรือเข้าถึงข้อมูลข่าสารได้อย่างไม่มีข้อจำกัดแค่วิทยุหรือโทรทัศน์เท่านั้น
                ยิ่งอุปกรณ์การสื่อสารที่มีการพัฒนาอย่างต่อเนื่องจากเดิมที่ต้องมีการใช้สายแลน
                (LAN) เพื่อเชื่อมต่อสัญญานอินเทอร์เน็ต
                มาเป็นสัญญานอินเทอร์เน็ตแบบไร้สาย (wireless)
                ก็ยิ่งทำให้การสื่อสารสะดวกสบายมากยิ่งขึ้นไปอีก
              </p>

              <p class="rem5">
                • สื่อดิจิทัลทำให้เราสามารถทำงานได้หลายอย่างพร้อมกัน
                ผ่านอุปกรณ์ที่มีความหลากหลาย และทำหน้าที่ได้หลายอย่างพร้อมกัน
                (Multi-mode, Multi-device และ Multi-tasking)
              </p>
              <p class="rem2">
                คำว่า Multi-mode และ Multi-tasking
                จะมีความหมายใกล้เคียงกันนั้นคือการทำงานด้วยความสามารถหลากหลายในเวลาเดียวกัน
                เช่น สมาร์ทโฟนหนึ่งเครื่องสามารถใช้ในการชม การฟัง
                การติดต่อสื่อสาร การพูด การเล่น ที่มีครบในเครื่องเดียวกันทั้งหมด
                สามารถทำงานพร้อมกันได้หลายอย่าง เช่น การเช็กข้อมูล e-mail
                กล่องข้อความใน facebook สื่อสารกับลูกค้าผ่าน Line
                จัดทำเอกสารในโปรแกรม Word โปรแกรม Excel หรือ presentation
                ด้วยโปรแกรม power point เป็นต้น
                โดยอาจจะผ่านอุปกรณ์ที่สามารถเชื่อมต่ออินเทอร์เน็ตได้เพียงเครื่องเดียว
                ซึ่งจะทำให้ประหยัดเวลา ได้งานที่มีจำนวนมากขึ้น
                ทำงานได้หลายอย่างได้ในเวลาเดียวกัน
                ทำให้ประหยัดเวลาและงบประมาณได้เป็นอย่างดี แต่มีงานวิจัยระบุว่า
                การทำงานหลายอย่างพร้อมกันตลอดเวลาอาจจะส่งผลต่อการลดศักยภาพในการจดจำ
                เพราะผู้ใช้ไม่ได้ฝึกให้สมองจดจ่ออยู่กับการทำงานอย่างใดอย่างหนึ่งเป็นเวลานานๆ
                และอาจจะมีผลต่อความหนาแน่นของเซลล์ประสาทในสมองส่วนที่ควบคุมการหยั่งรู้
                ความเข้าใจ ความรู้สึก และการควบคุมอารมณ์ตนเอง
                นอกจากนี้ยังทำให้เกิดภาวะ สมาธิสั้น หลงลืมบ่อยอีกด้วย (อรวรรณ,
                2563)
                อย่างไรก็ตามการมีอุปกรณ์ที่สามารถตอบสนองการทำงานที่หลากหลายถือเป็นประโยชน์หากผู้ใช้สามารถบริหารจัดการในการใช้สื่อได้อย่างมีประสิทธิภาพ
              </p>
              <p class="rem2">
                นอกจากการเข้าใจหลักการพื้นฐานที่สำคัญในยุคดิจิทัลแล้ว
                การเข้าใจองค์กระกอบของกระบวนการในการสื่อสารดิทัลจะทำให้เราสามารถใช้สื่อดิจิทัลได้อย่างมีคุณภาพและบรรลุเป้าหมายที่วางไว้ได้
              </p>
              <br />
              <h3>2. องค์ประกอบของการสื่อสารในยุคดิจิทัล</h3>
              <p class="rem2">
                แรกเริ่มเดิมทีองค์ประกอบการที่สำคัญและเป็นพื้นฐานประกอบ
                ผู้ส่งสาร (Sender) สาร (Message) สื่อ (Channel) ผู้รับสาร
                (Receiver) และการสื่อสารตอบกลับ
                การสื่อสารที่ประสบความสำเร็จคือการที่ผู้ส่งสารสามารถส่งเนื้อหาสารไปยังผู้รับสารจนเกิดเป็นความเข้าใจที่ตรงกัน
                ชัดเจน และบรรลุเป้าหมายที่จะสื่อสารนั้น
                เมื่อยุคสมัยเปลี่ยนไปเกิดเทคโนโลยีดิจิทัลเข้ามาแทนที่รูปแบบการสื่อสารแบบดั้งเดิมก็ส่งผลให้องค์ประกอบการสื่อสารเปลี่ยนแปลงเช่นกัน
              </p>
              <h4 class="rem2">
                2.1 องค์ประกอบด้านการสื่อสารดิจิทัล :
                การเปลี่ยนผ่านจากสื่อเก่าสู่สื่อใหม่
              </h4>
              <p class="rem5">
                เมื่อช่องทางการสื่อสารเกิดการเปลี่ยนแปลงเข้าสู่ยุคดิจิทัล
                โดยมีการสื่อสารผ่านเครือข่ายอินทอร์เน็ต
                ผ่านรูปแบบการสื่อสารใหม่ที่มนุษย์ได้พัฒนาขึ้น เช่น การ
                พูดคุยผ่านการสนทนาข้อความ (Chatting) การเข้าสู่
                เครือข่ายสังคมออนไลน์ (Social Networking)
                หรือการพูดคุยกันโดยเชื่อมต่อกับอินเทอร์เน็ต (Free Call)
                ไปจนถึงการพูดคุยแบบเห็นหน้ากัน (Video Call)
                จึงส่งผลให้องค์ประกอบของการสื่อสารมีการเปลี่ยนแลงไปตามเทคโนโลยีดิจิทัล
                ดังต่อไปนี้
              </p>
              <h5 class="rem5">
                2.2.1 การหลอมรวมระหว่างผู้ส่งสารและผู้รับสาร
                กลายเป็นผู้สร้างสรรค์สื่อ (User Generated Content: UCG)
              </h5>
              <p class="rem5">
                ด้วยลักษณะของสื่อดิจิทัลที่ผู้ส่งสารและผู้รับสาร
                สามารถสลับบทบาทในการสื่อสารได้ทันที มีการโต้ตอบ
                และมีปฏิสัมพันธ์ต่อการสื่อสาร
                ที่ทุกคนสามารถเป็นผู้ส่งสารและรับสารได้ในเวลาเดียวกัน
                ทำให้เส้นแบ่งระหว่างผู้ส่งสารและผู้รับสารได้ถูกทำลายลงไป
                ผู้ใช้ออนไลน์จะสามารถสร้างสรรค์หรือสื่อสารเนื้อหาได้อย่างอิสระผ่านช่องทางต่างๆ
                หลากหลายรูปแบบ สามารถจัดกลุ่มรูปแบบการสื่อสารแบบ UCG ได้ดังนี้
              </p>

              <p class="rem5">
                1) กระดานสนทนา เว็บบอร์ด ฟอรั่ม เช่น Pantip.com, Dek-D.com
              </p>
              <p class="rem5">
                2) เว็บล็อก/ไดอารี่ออนไลน์ เช่น Wordpress, Blogger, Tumbler
              </p>
              <p class="rem5">3) สาราณุกรมออนไลน์ เช่น Wikipedia</p>
              <p class="rem5">
                4) เครือข่ายสังคมออนไลน์ เช่น Facebook, Twitter, Instagram,
                Pinterest
              </p>
              <p class="rem5">
                5) ชุมชนแฟน เช่น แฟนเพจต่างๆ อาทิ ภาพยนตร์ ละคร ดาหรา นักร้อง
                สโมสรฟุตบอล ดนตรี และกีฬา เป็นต้น
              </p>
              <br />
              <p class="rem5">
                อย่างไรก็ตาม การที่ใครๆ ก็สามารถกลายเป็นผู้สร้างสรรค์เนื้อหานี้
                อาจจะทำให้ความเป็นมืออาชีพลดลง
                รูปแบบการสื่อสารเป็นแบบเพื่อนต่อเพื่อน (Peer to peer)
                ไม่เป็นทางการ ทุกคนมีอิสระเต็มที่ในการสื่อสาร
                เมื่อประกอบกับตัวพื้นที่ในรูปแบบของ UCG
                ก็เอื้ออำนวยความง่ายในการจัดทำสื่อและสื่อสารออกไปดังที่กล่าวข้างต้น
                บนโลกออนไลน์จึงเต็มไปด้วยข้อมูลจำนวนมากมายมหาศาลจากทั่วทุกมุมมองโลก
                ดังที่ โธมัส ฟริดแมน (Thomas Friedman) ได้เขียนไว้ในหนังสือ
                “โลกของเรานั้นแบน: ประวัติศาสตร์โดยย่อของศตวรรษที่ยี่สิบเอ็ด
                (The World Is Flat: A Brief History of the Twenty-first
                Century)” โดยหนังสือเล่มนี้รู้จักในนาม “ใครว่าโลกกลม”
                ว่าการเกิดขึ้นของโปรแกรมท่องอินเทอร์เน็ตอย่างเนตสเคป (Netscape)
                ทำให้ผู้คนเข้าถึงข้อมูลทางอินเทอร์เน็ตได้สะดวกขึ้น
                ฟริดแมนเขียนไว้ชัดว่า “เวทีที่เกิดขึ้นใหม่นี้เป็นเวทีระดับโลก
                บนพื้นฐานของการติดต่อผ่านอินเทอร์เน็ต
                ที่ส่งผลให้เกิดความร่วมมือกันในหลายระดับ
                โดยเฉพาะการแบ่งปันความรู้ และแบ่งงานกันทำ ในเวลาเดียวกัน
                โดยไม่คำนึงถึงความห่างไกลด้านภูมิศาสตร์ หรือภาษา”
              </p>
              <p class="rem5">
                อย่างไรก็ตาม
                ในด้านหนึ่งการสื่อสารที่มาจากคนหลากหลายและมีเสรีภาพได้ส่งผลให้โลกออนไลน์ถูกนำมาใช้ในทางที่ก่อให้เกิดความเสียหายจากมิจฉาชีพ
                เช่น ข้อมูลข่าวสารที่อาจจะเป็นเท็จ ข่าวปลอม (Fake News)
                โฆษณาชวนเชื่อ การต้มตุ๋นผู้อื่น (Scammer)
                รวมถึงการเข้าไปลักลอบขโมยข้อมูลทางด้านการเงินหรือข้อมูลสำคัญ
                (Hack)
                ซึ่งผู้ที่ใช้สื่อดิจิทัลจำเป็นจะต้องมีความรู้พื้นฐานในการใช้สื่อดิจิทัลอย่างปลอดภัยและรู้เท่าทัน
                (Digital security and Digital Literacy)
                รวมถึงนำสื่อดิจิทัลไปใช้ให้เกิดประโยชน์แก่ตนเองและผู้อื่น
                โดยผู้ใช้สารจะต้องใช้วิจารณญาณรวมถึงการคิดวิเคราะห์อย่างสูงในการเลือกข้อมูลข่าวสารที่สอดคล้องกับความต้องการของตนเอง
                ปลอดภัย และมีประโยชน์อีกด้วย
              </p>
              <br />
              <h5 class="rem5">2.2.2 การปฏิสัมพันธ์กับเนื้อหาแบบใหม่</h5>
              <p class="rem5">
                จากเดินรูปแบบการสื่อสารจะเป็นลักษณะการที่ผู้ส่งสารจัดทำเนื้อหาสารแล้วส่งไปยังผู้รับสารแล้วรอการตอบกลับ
                แต่สำหรับสื่อดิจิทัลได้สร้างให้เกิดการมีปฎิสัมพันธ์
                (Interactive) รูปแบบใหม่ ด้วยรูปแบบการแบ่งปัน
                การนำเนื้อหามาจัดกระทำใหม่ หรือสร้างขึ้นมาใหม่ (share, mix,
                remix) ทำให้เกิดการการกระจายเนื้อหาไปในวงกว้าง
                นอกจากนี้ยังสามารถมีส่วนร่วมด้วยการเข้าไปแสดงความรู้สึก
                และความคิดเห็นด้วยการกด Like, Comment หรือ Share
                ได้อย่างอิสระเสรี โดยทำให้มีการตอบโต้กันระหว่างผู้ใช้สาร
                และมีเครื่องมือให้ผู้รับสารสามารถเลือกรับข่าวสารในรูปแบบที่ต้องการได้
                (interactivity) และสามารถเชื่อมโยงประเด็นนั้นๆไปสู่ประเด็นอื่น ๆ
                หรือสามารถหาข้อมูลได้ลงลึกมากยิ่งขึ้นผ่านลิงค์ต่างๆ (Hyperlink)
                มีความสามารถในการเชื่อมต่อข้อมูล (hypertextuality)
                รูปแบบการสื่อสารบนโลกออนไลน์จึงเป็นเรื่องของการแบง่ปัน
                การรับฟังและตอบโต้กับผู้อื่นทำให้มีการแลกเปลี่ยนประสบการณ์
                ความรู้ การสร้างความสัมพันธ์
                และการรวบรวมความรู้มาจากแหล่งที่หลากหลาย
                เป็นเครือข่ายทำให้คนสามารถแสดงความเห็นส่งข้อมลูได้อย่างอสิระและก้าวข้ามข้อจำกัดของสื่อดั้งเดิมได้
              </p>
              <p class="rem5">
                ดังนั้น
                ทุกคนบนโลกออนไลน์จะมีโอกาสในการสร้างเนื้อหาแบบใหม่รวมถึงการเข้าไปเป็นส่วนหนึ่งบนสังคมออนไลน์โดยที่อาจจะไม่เคยรู้จักกันมาก่อน
                การมีปฏิสัมพันธ์แบบใหม่จะสร้างให้เกิดเป็นวัฒนธรรมการมีส่วนร่วม
                (Participatory Culture) ของผู้คนบนโลกออนไลน์มากขึ้น
                ผู้รับสารอาจจะกลายเป็นผู้สร้าง และผู้ส่งอาจจะกลายเป็นผู้รับ
                หรือกลายเป็นผู้ติดตาม (Followers)
                หรือเป็นผู้ที่นำเนื้อหาที่ได้มาจัดกระทำขึ้นใหม่
                ข้อมูลที่ส่งผ่านมีปฏิสัมพันธ์เนื้อหาแบบใหม่นี้เองที่จะให้เกิดอิสรภาพในการแสดงความคิดเห็น
                การโต้ตอบ หรือการแสดงความไม่เห็นด้วยกรณีที่เป็นความเห็นต่าง
                ทั้งนี้
                เพราะแพลตฟอร์มดิจิทัลได้เปิดโอกาสให้ทุกคนได้เข้าไปมีส่วนร่วมอย่างเต็มที่
                มีอิสระในการแสดงความคิดเห็น (Freedom of Expression)
                ช่วยสะท้อนมุมมองความเป็นจริงที่เราอาจจะไม่เคยรู้มาก่อน
                และอาจจะนำมาซึ่งการเปลี่ยนแปลงสังคมไปในทิศทางที่ดีขึ้น
                อย่างไรก็ตาม หากผู้ที่ใช้สื่อเป็นผู้ที่ขาดจริยธรรม
                หรือขาดความรับผิดชอบต่อสังคม สิ่งที่อาจจะตามมาคือ
                การนำเสนอข้อมูลข่าวสารหรือการส่งข้อมูลข่าวสารออกไปอย่างผิด ๆ
                หากเป็นกรณีของการใช้สื่อออนไลน์เพื่อกลั่นแกล้งบุคคลอื่นบนโลกออนไลน์
                หรือการบิดเบือนข้อเท็จจริง
                ก็จะส่งผลกระทบอย่างรุนแรงต่อผู้ที่เกี่ยวข้อง
                เพราะการที่คนสามารถเข้ามาแลกเปลี่ยนและมีปฏิสัมพันธ์
                หากไม่ผ่านการกลั่นกรอง การตรวจสอบข้อเท็จจริง
                การใช้กระบวนการคิดมีวิจารณญาณ แต่เน้นอารมณ์
                พื้นที่ออนไลน์ก็จะเต็มไปด้วยเนื้อหาที่เต็มไปด้วยความรุนแรง
                ข่าวลวง หรือการกลั่นแกล้งผู้อื่น
                และเป็นพื้นในการสร้างมลพิษทางการสื่อสารให้กับสังคมได้
              </p>

              <br />
              <h5 class="rem5">
                2.2.3 ช่องทางในการสื่อสาร (Channel) แบบใหม่: Smart Devices
              </h5>
              <p class="rem5">
                ในสมัยอดีดช่องทางที่ใช้ติดต่อสื่อสารอาจจะมีข้อจำกัด เช่น
                หากเป็นโทรศัพท์ก็ใช้เพียงแค่โทรติดต่อสื่อสารอย่างเดียว
                และการฟังจะเป็นฟังจากเครื่องรับวิทยุ
                ส่วนการชมจะใช้เครื่องรับโทรทัศน์หรือภาพยนตร์
                แต่ในยุคดิจิทัลทุกผัสสะการเขียน การอ่าน การฟัง การพูด
                ได้ถูกนำมาหลอมรวมไว้ในการสื่อสารผ่านอุปกรณ์เพียงเครื่องเดียวนั่นคือ
                สมาร์ทโฟน (Smartphone) หรือ อุปกรณ์อัจฉริยะ (Smart Devices)
                ที่สามารถตอบสนองการสื่อสารได้ทุกรูปแบบ
                ช่องทางการสื่อสารแบบใหม่นี้จึงได้เข้ามาทำลายข้อจำกัดในการสื่อสารและทำให้เกิดความสะดวกสบายทางการสื่อสารอย่างที่ไม่เคยเป็นมาก่อน
              </p>

              <br />
              <h5 class="rem5">
                2.2.4 จากเนื้อหาสาร (Messages) ไปสู่ดิจิทัลคอนเทนต์ (Digital
                Content)
              </h5>
              <p class="rem5">
                เนื้อหาสารแบบใหม่เกิดจากการสร้างขึ้นด้วยชุมชน หรือ
                พลเมืองบนโลกออนไลน์
                ซึ่งคนเหล่านั้นอาจจะสร้างเนื้อหาขึ้นมาเพื่อตอบโจทย์กลุ่มเฉพาะ
                หรือสามารถตอบโจทย์ความต้องการของผู้คนบนโลกออนไลน์
                แต่ประเด็นที่สำคัญคือควาพยายามในการการสร้างเนื้อหาที่กระตุ้นให้คนอยากติดตาม
                อยากไปมีส่วนร่วม (Engagement) ซึ่งหมายถึง
                การสร้างให้เกิดความสนใจและความผูกผันต่อเนื้อหานั้นจนกลายเป็นผู้ติดตามอย่างเหนียวแน่น
                และส่งต่อข้อมูล (share) ไปยังคนอื่นๆ
                หรืออาจจัดทำเนื้อหาขึ้นมาใหม่ภายใต้เนื้อหาเดิมเพื่อสร้างให้เกิดความสนใจและเป็นกระแสที่น่าติดตามมากขึ้นบนโลกออนไลน์
                อย่างไรก็ตามมีข้อที่พึงระวัง คือ
                บนโลกออนไลน์มีผู้คนหลากหลายและมีอิสระในการสื่อสาร
                สามารถเป็นผู้ผลิตสื่อและกระจายเนื้อหา
                อาจจะทำให้มิจฉาชีพใช้เป็นช่องทางในการส่งข้อมูลหรือเนื้อหาที่อาจจะเป็นเท็จ
                หรือเนื้อหาที่ทำร้ายหรือทำลายชื่อเสียงผู้อื่น
                และในบางคนอาจจะใช้เป็นพื้นที่ในการส่งเนื้อหาที่เป็นการกระทำผิด
                หรือล่อลวงให้คนหลงเชื่อจนนำไปสู่ความเสียหายได้เช่นกัน
              </p>

              <br />
              <h5 class="rem5">
                2.2.5 จากผู้รับสาร (Receiver) เป็นนักสำรวจ (Discover)
              </h5>
              <p class="rem5">
                สื่อดิจิทัลได้ทำให้ผู้รับสารได้มีพลังมากขึ้น ดังนั้น
                ผู้รับสารกลายมาเป็นผู้ค้นพบข้อมูล (Discover)
                และมีอำนาจในการเลือกเนือหาที่มาตอบความต้องการของตนเองมากที่สุด
                จากการที่เป็นผู้รับสารแบบตั้งรับ (passive audiences)
                กลายมาเป็นผู้ที่รับสารแบบกระตือรือร้น (active users)
                และยังสามารถแนะนำเนื้อหาสารนั้นๆ
                ส่งไปให้ผู้อื่นด้วยการกระจายข้อมูลข่าวารออกไปอย่างไม่สิ้นสุด
                การที่เป็นผู้แนะนำและสามารถส่งได้อย่างไม่สิ้นสุดนี้เอง
                ผู้ใช้สารต้องมีทักษะความคิดวิเคราะห์ในการเลือก
                ข้อมูลข่าวสารที่เป็นประโยชน์และมีความรับผิดชอบสูงมาก
              </p>
              <p class="rem5">
                จากองค์ประกอบทั้งหมดข้างต้น Pricewaterhouse Cooper
                ให้นิยามของการสื่อสารในยุคดิจิทัลไว้ว่า “Life Style Media”
                (2006)
                โดยการสื่อสารในยุคดิจิทัลโดยผู้ใช้สารเป็นผู้ที่เข้ามามีบทบาทสำคัญในการกำหนดเนื้อหาสาร
                ที่มิใช่เป็นเพียงการสื่อสารแบบตั้งรับ (passive)
                แต่กลายเป็นคนที่สามารถเป็นผู้เลือกค้นพบ จัดกระทำ ส่งต่อ
                และกระจายเนื้อหาข่าวสารได้อย่างไม่มีที่สิ้นสุด
                ซึ่งถือเป็นการปรับมุมมองใหม่ของกระบวนการสื่อสารในยุคดิจิทัล
                เทคโนโลยีจะเข้ามาเป็นตัวเชื่อมช่องว่างระหว่างความเป็นมืออาชีพและความสามารถในการสร้างสรรค์เนื้อหาตามความต้องการของผู้รับสาร
                (on demand) ผู้รับสารจะมีความสามารถในการผลิตเนื้อหาได้เอง
                ซึ่งอาจจะกล่าวได้ว่า
                ผู้ใช้สารบนโลกออนไลน์มีอำนาจในการควบคุมเนื้อหา
                มีความสามารถในการผลิตเนื้อหาสารได้เอง
                และมีอำนาจในการเผยแพร่ออกไปอย่างกว้างขวางบนโลกออนไลน์ (social
                network)
                ผ่านช่องทางใหม่คืออุปกรณ์ดิจิทัลที่มีการเชื่อมต่ออินเทอรณ์เน็ตที่สามารถเข้าถึงได้ทุกที่ทุกเวลา
                โดยองค์ประกอบการสื่อสารแบบ Life Style Media (สุจิตรา, 2556)
                แสดงในแผนภูมิด้านล่าง
              </p>
