                บนเครือข่ายบนระบบคอมพิวเตอร์
                เครือข่ายคอมพิวเตอร์ที่ผู้ใช้อินเทอร์เน็ตเรียกเข้ามิได้เป็นเพียงเครือข่ายขององค์กรที่ผู้ใช้สังกัด
                แต่เป็นการเชื่อมโยงของเครือข่ายต่างๆ
                เข้าหากันหลายพันหลายหมื่นเครือข่ายมีข้อมูลข่าวสารอยู่ระหว่างเครือข่ายเป็นจำนวนมาก
                การส่งข่าวสารในเครือข่ายนั้นอาจทำให้ข่าวสารกระจายเดินทางไปยังเครือข่ายอื่น
                ๆ
                อีกเป็นจำนวนมากหรือแม้แต่การส่งไปรษณีย์อิเล็กทรอนิกส์ฉบับหนึ่งก็อาจจะต้องเดินทางผ่านเครือข่ายอีกหลายเครือข่ายกว่าจะถึงปลายทาง
                ดังนั้นผู้ใช้บริการต้องให้ความสำคัญและตระหนักถึงปัญหาปริมาณข้อมูลข่าวสารที่วิ่งอยู่บนเครือข่ายการใช้งานอย่างสร้างสรรค์และเกิดประโยชน์จะทำให้สังคมอินเทอร์เน็ตน่าใช้และเป็นประโยชน์ร่วมกันอย่างดี
                กิจกรรมบางอย่างที่ไม่ควรปฏิบัติจะต้องหลีกเลี่ยงเช่นการส่งกระจายข่าวไปเป็นจำนวนมากบนเครือข่าย
                การส่งเอกสารจดหมายลูกโซ่ ฯลฯ
                สิ่งเหล่านี้จะเป็นผลเสียโดยรวมต่อผู้ใช้และไม่เกิดประโยชน์ใด ๆ
                ต่อสังคมอินเทอร์เน็ต
              </p>
              <p class="rem5">
                เพื่อให้การอยู่ร่วมกันในสังคมอินเทอร์เน็ตสงบสุข Arlene H.Rinaldi
                แห่งมหาวิทยาลัย ฟอร์ริดาแอตแลนติก
                จึงรวบรวมกฎกติกามารยาทและวางเป็นจรรยาบรรณอินเทอร์เน็ตหรือที่เรียกว่า
                Netiquette ไว้ดังนี้
              </p>

              <h5 class="rem2">
                จรรยาบรรณที่ผู้ใช้อินเทอร์เน็ตยึดถือไว้เป็นบทการปฏิบัติเพื่อเตือนความจำ
              </h5>
              <p class="rem5">
                1. ต้องไม่ใช้คอมพิวเตอร์ทำร้ายหรือละเมิดผู้อื่น
              </p>
              <p class="rem5">2. ต้องไม่รบกวนการทำงานของผู้อื่น</p>
              <p class="rem5">
                3. ต้องไม่สอดแนมหรือแก้ไขเปิดดูในแฟ้มของผู้อื่น
              </p>
              <p class="rem5">
                4. ต้องไม่ใช้คอมพิวเตอร์เพื่อการโจรกรรมข้อมูลข่าวสาร
              </p>
              <p class="rem5">
                5. ต้องไม่ใช้คอมพิวเตอร์สร้างหลักฐานที่เป็นเท็จ
              </p>
              <p class="rem5">6. ต้องไม่คัดลอกโปรแกรมผู้อื่นที่มีลิขสิทธิ์</p>
              <p class="rem5">
                7. ต้องไม่ละเมิดการใช้ทรัพยากรคอมพิวเตอร์โดยที่ตนเองไม่มีสิทธิ์
              </p>
              <p class="rem5">8. ต้องไม่นำเอาผลงานของผู้อื่นมาเป็นของตน</p>
              <p class="rem5">
                9. ต้องคำนึงถึงสิ่งที่จะเกิดขึ้นกับสังคมอันติดตามมาจากการกระทำ
              </p>
              <p class="rem5">
                10.ต้องใช้คอมพิวเตอร์โดยเคารพกฎระเบียบ กติกามารยาท
              </p>
              <br />
              <p class="rem5">
                จรรยาบรรณเป็นสิ่งที่ทำให้สังคมอินเทอร์เน็ตเป็นระเบียบความรับผิดชอบต่อสังคมเป็นเรื่องที่จะต้องปลูกฝังกฎเกณฑ์ของแต่ละเครือข่ายจึงต้องมีการวางระเบียบเพื่อให้การดำเนินงานเป็นไปอย่างมีระบบและเอื้อประโยชน์ซึ่งกันและกัน
                บางเครือข่ายมีบทลงโทษและจรรยาบรรณที่ชัดเจน
                เพื่อช่วยให้สังคมสงบสุขและหากการละเมิดรุนแรงกฎหมายก็จะเข้ามามีบทบาทได้เช่นกัน
              </p>
              <p class="rem5 text-justify">
                กฎหมายและศีลธรรม (Motal)
                เป็นกฏเกณฑ์ที่เป็นลายลักษณ์อักษรของสังคมมาช้านานเราพอเข้าใจได้ง่าย
                ๆ ว่า
                รัฐเป็นผู้ตรากฏหมายขึ้นเพื่อใช้บังคับพลเมืองในอาณาเขตของรัฐ
                ขณะศีลธรรมเป็นข้อบัญญัติทางศาสนาซึ่งเป็นหลักความเชื่อของประชาชน
                จริยธรรม (Ethics) เป็นเรื่องของการกำหนดความถูกต้องดีงาม
                สิ่งที่ไม่ควรทำ มีหลักปฏิบัติในระดับที่สูงกว่ามารยาทในสังคม เช่น
                คนที่ไม่ยอมเข้าแถวเพื่อขอรับบริการตามสิทธิ์ก่อนหลังอาจถือว่าไม่มีมารยาทหรือพนักงานคอมพิวเตอร์คนหนึ่งเอาข้อมูลทางการเงินของลูกค้าที่เขาจะต้องเห็นตามหน้าที่การงานไปหาผลประโยชน์แก่ตนเอง
                เช่น ขายรายชื่อนั้นให้ธุรกิจอื่น
                หรือบอกให้แก่คู่สมรสซึ่งเป็นพนักงานขายตรงไปเสนอขายสินค้า
                การกระทำเช่นนี้ถือว่าไม่ถูกต้อง ไม่มีจริยธรรม จริงอยู่
                แม้ว่าบริษัทที่พนักงานผู้นั้นทำงานอยู่จะไม่เสียหาย
                แต่การนำเอาของบริษัทไปใช้เพื่อประโยชน์ส่วนตัวก็เป็นสิ่งที่ไม่อาจทำได้อย่างเปิดเผย
                หรือพนักงานขายสินค้าของทางบริการหนึ่งซึ่งลาออกจากบริษัทเพื่อไปทำงานกับบริษัทคู่แข่งแล้วใช้ประโยชน์จากความรู้ในเรื่องข้อมูลราคาหรือความลับทางการค้าของบริษัทแรกไปให้บริษัทหลัง
                ก็อาจเรียกได้ว่าพนักงานคนนั้นไม่มีจริยธรรม
                เมื่อสังคมสลับซับซ้อนขึ้น มีการแบ่งหน้าที่กันออกเป็นหน้าที่ต่าง
                ๆ จึงมีข้อกำหนดที่เรียกว่า “จรรยาวิชาชีพ” (Code of Conduct) ขึ้น
                เพื่อใช้เป็นหลักปฏิบัติของคนในอาชีพนั้น ๆ เราคงเคยได้ยิน
                จรรยาบรรณของแพทย์ ที่จะไม่เปิดเผยเรื่องราวส่วนตัวของคนไข้
                จรรยาบรรณของนักหนังสือพิมพ์ที่รับเงินทองสิ่งตอบแทนเพื่อเสนอข่าวหรือไม่เสนอข่าวไม่เปิดเผยแหล่งข่าวถ้าแหล่งข่าวไม่ต้องการจรรยาบรรณวิชาชีพของสถาปนิกหรือวิศวกรผู้ออกแบบที่ต้องไม่รับผลประโยชน์ใด
                ๆ จากผู้ขายอุปกรณ์ที่ใช้ในงานที่เขาออกแบบ
                ซึ่งขายให้กับผู้ว่าจ้างงานชิ้นนั้นเพราะเขาได้รับปลตอบแทนจากผู้ว่าจ้างแล้ว
                จรรยาบรรณของวิชาชีพใด ก็มักกำหนดขึ้นโดยสมาคมวิชาชีพนั้น
                โดยมีข้อกำหนด บทลงโทษที่นอกเหนือไปจากกฏหมายบ้านเมือง เช่น
                เพิกถอนสมาชิกภาพ เพิกถอนหรือพักใบประกอบวิชาชีพ
                และอาจมีกฏหมายรองรับอีกด้วย อาชีพนักคอมพิวเตอร์
                เป็นอาชีพใหม่ในสังคมสารสนเทศ การใช้คอมพิวเตอร์และระบบสารสนเทศ
                ก็เป็นสิ่งใหม่ที่มีศีลธรรม จริยธรรม
                และจรรยาบรรณเฉพาะของตนซึ่งบางครั้งก็แตกต่างจากจริยธรรมที่ยอมรับกันมาแต่ก่อน
                หลักพื้นฐานของจริยธรรมในสังคมสารสนเทศก็คือการเคารพผู้อื่น
                เคารพความเป็นส่วนตัว
                การเข้าถึงระบบคอมพิวเตอร์และข้อมูลก็จะเฉพาะสิทธิ์ที่ตนเองมีในส่วนที่เกี่ยวข้องกับงาน
              </p>

              <h5 class="rem2">
