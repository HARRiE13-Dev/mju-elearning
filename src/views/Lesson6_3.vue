              <h4>
                สมาชิกสมาคมเครื่องจักรกลคอมพิวเตอร์ (Association of
                ComputerMachinery ACM Code of Conduct)
              </h4>
              <br />
              <h5 class="rem2">1. กฎข้อบังคับทางศีลธรรมทั่วไป</h5>
              <p class="rem5 text-justify">
                1. ทำประโยชน์ให้สังคมและความผาสุกของมนุษย์
                ข้อนี้เกี่ยวกับคุณภาพชีวิตของประชาชนทุกคนคุ้มครองหลักสิทธิมนุษยชน
                ขั้นพื้นฐาน เคารพความหลากหลายของวัฒนธรรมทั้งหมด
                ลดผลด้านลบของระบบคอมพิวเตอร์ที่มีต่อสุขภาพอนามัยและความปลอดภัยรับผิดชอบต่อสังคม
                และสิ่งแวดล้อม
              </p>
              <p class="rem5 text-justify">
                2. ไม่ทำอันตรายแก่ผู้อื่น อันตรายหมายรวมถึง
                การบาดเจ็บหรือผลต่อเนื่องด้านลบ เช่น
                การสูญเสียข้อมูลอันเป็นที่ไม่พึงปรารถนา
                ทรัพย์สินสูญหายหรือเสียหาย ผลกระทบต่อสิ่งแวดล้อมที่ไม่พึงปรารถนา
                หลักการข้อนี้ห้ามการใช้เทคโนโลยีสารสนเทศไปทำอันตรายต่อผู้ใช้สาธารณชน
                พนักงานและนายจ้างอันตรายนี้รวมถึงการจงใจทำลายหรือแก้ไขข้อมูลในแฟ้มข้อมูลและโปรแกรมที่ทำให้สูญเสีย
                หรือเสียเวลาและความพยายามของบุคลากรที่จำเป็นต้องใช้ทำลายไวรัสคอมพิวเตอร์ในสภาพแวดล้อมที่ทำงาน
                นักวิชาชีพคอมพิวเตอร์จะต้องรายงานสัญญาณอันตรายที่อาจก่อให้เกิดผลต่อความเสียหายของสังคมและบุคคล
                แม้ว่าหัวหน้างานจะไม่ลงมือแก้ไขหรือลดทอนอันตรายนั้น
                ก็อาจจำเป็นต้องแจ้งให้ผู้อื่นที่เกี่ยวข้องทราบโดยอาจอาศัยผู้ร่วมวิชาชีพเป็นผู้ให้คำปรึกษา
              </p>
              <p class="rem5">
                3. ซื่อสัตย์และไว้วางใจได้
                นักคอมพิวเตอร์ที่ซื่อสัตย์นอกจากจะไม่จงใจแอบอ้างระบบหรือการออกแบบที่หลอกลวงอันเป็นเท็จแล้ว
                ยังจะต้องเปิดเผยอย่างเต็มที่ให้เห็นข้อจำกัดและปัญหาที่เกี่ยวข้องกับระบบทั้งหมดอีกด้วย
              </p>
              <p class="rem5">
                5. ให้เกียรติสิทธิในทรัพย์สิน รวมทั้งลิขสิทธิ์และสิทธิ์บัตร
                แม้ว่าสิ่งซึ่งมีลิขสิทธิ์ สิทธิบัตร ความลับทางการค้า
                การละเมิดข้อตกลงการใช้สิทธิ จะได้รับการคุ้มครองทางกฏหมายอยู่แล้ว
                แม้แต่ซอฟต์แวร์ที่ไม่ได้รับการคุ้มครอง
                การละเมิดก็ถือว่าเป็นการขัดต่อการประพฤติทางวิชาชีพ
                การลอกหรือทำสำเนาซอฟต์แวร์จะต้องทำโดยมีอำนาจหน้าที่เท่านั้น
                การทำสำเนาวัสดุใด ๆ เป็นสิ่งที่ให้อภัยไม่ได้
              </p>
              <p class="rem5">
                4. ยุติธรรมและการกระทำที่ไม่แบ่งแยกกีดกัน
                ข้อบังคับข้อนี้ใช้คุณค่าของความเสมอภาค ความใจกว้างให้อภัย
                เคารพในผู้อื่น ความเที่ยงธรรม การแบ่งแยกกีดกันโดยเชื้อชาติ เพศ
                ศาสนา อายุ ความพิการ สัญชาติ
                หรือปัจจัยอื่นเป็นสิ่งที่ไม่อาจยอมรับได้
              </p>
              <p class="rem5">
                6. ให้เกียรติแก่ทรัพย์สินทางปัญญา
                นักวิชาชีพคอมพิวเตอร์จะต้องป้องกันหลักคุณธรรมของทรัพย์สินทางปัญญา
                แม้ว่างานนั้นจะไม่ได้รับการป้องกันอย่างเปิดเผยก็ตาม เช่น
                งานอันมีลิขสิทธิ์หรือสิทธิบัตร
              </p>
              <p class="rem5">
                7. เคารพความเป็นส่วนตัวของผู้อื่น หลักการนี้ยังหมายถึง
                การเก็บข้อมูลส่วนบุคคลไว้ในระบบเท่าที่จำเป็น
                มีระยะเวลากำหนดการเก็บรักษาและทิ้งอย่างชัดเจน
                และปฏิบัติตามอย่างเคร่งครัด
                การรวบรวมข้อมูลไว้เพื่อวัตถุประสงค์หนึ่ง ข้อมูลนั้นจะถูกนำไปใช้
                เพื่อการอื่นโดยไม่ได้รับคำยินยอมจากผู้นั้นมิได้
              </p>
              <p class="rem5">
                8. ให้เกียรติในการรักษาความลับ
                หลักแห่งความซื่อสัตย์ข้อนี้ขยายไปถึงความลับของข้อมูลที่ไม่ว่าจะแจ้งโดยเปิดเผยหรือสัญญว่าจะปกปิดเป็นความลับ
                หรือโดยนัยเมื่อข้อมูลส่วนตัวที่ไม่เกี่ยวข้องกับหน้าที่ของผู้นั้นปรากฏขึ้น
                จริยธรรมข้อนี้เกี่ยวข้องกับการเคารพข้อบังคับทั้งหลายที่เกี่ยวกับความลับของขายจ้าง
                ลูกค้า ผู้ใช้
                เว้นเสียแต่เปิดเผยโดยกฏหมายบังคับหรือตามหลักแห่งจรรยาบรรณนี้
              </p>
              <br />
              <h5 class="rem2">2. ความรับผิดชอบในวิชาชีพ</h5>
              <p class="rem5">
                1. มุ่งมั่นเพื่อให้ได้คุณภาพที่ดีที่สุด
                และให้ตระหนักถึงผลเสียหายที่สืบเนื่องจากระบบที่ด้อยคุณภาพ
              </p>
              <p class="rem5">
                2. ได้มาและรักษาไว้ซึ่งความเชี่ยวชาญแห่งวิชาชีพ
              </p>
              <p class="rem5">
                3. รับรู้และเคารพกฎหมายท้องถิ่น กฎหมายแห่งรัฐ
                และกฎหมายระหว่างประเทศ
              </p>
              <p class="rem5">
                4. ยอมรับและจัดให้มีการสอบทานทางวิชาชีพ (Professional Review)
              </p>
              <p class="rem5">
                5.
                ให้ความเห็นประเมินระบบคอมพิวเตอร์และผลกระทบอย่างละเอียดครบถ้วน
                รวมทั้งการวิเคราะห์ความเสี่ยงที่เป็นไปได้
              </p>
              <p class="rem5">
                6. ให้เกียรติ รักษาสัญญา ข้อตกลง
                และความรับผิดชอบที่ได้รับมอบหมาย
              </p>
              <p class="rem5">
                7. ปรับปรุงความเข้าใจของสาธารณชนต่อคอมพิวเตอร์และผลสืบเนื่อง
              </p>
              <p class="rem5">
                8.
                เข้าถึงทรัพยากรคอมพิวเตอร์และสื่บสารเฉพาะเมื่อได้รับมอบอำนาจตามหน้าที่เท่านั้นไม่ใช้ระบบคอมพิวเตอร์ของผู้อื่น
                ซอฟต์แวร์ แฟ้มข้อมูลใด ๆ โดยไม่ได้ขออนุญาต
              </p>
              <br />
              <h5 class="rem2">3. จริยธรรมในการใช้ไปรษณีย์อิเล็กทรอนิกส์</h5>
              <p class="rem5">1. ไม่โฆษณาหรือเสนอขายสินค้า</p>
              <p class="rem5">2. รู้ตัวว่ากำลังกล่าวอะไร</p>
              <p class="rem5">
                3. ถ้าไม่เห็นด้วยกับหลักพื้นฐานของรายชื่อกลุ่มที่ตนเป็นสมาชิก
                ก็ควรออกจากกลุ่มไม่ควรโต้แย้ง
              </p>
              <p class="rem5">4. คิดก่อนเขียน</p>
              <p class="rem5">5. อย่าใช้อารมณ์</p>
              <p class="rem5">6. พยายามอ่านคำถามที่ถามบรอย (FAQ) ก่อนเสมอ</p>
              <p class="rem5">
                7. ไม่ส่งข่าวสารที่กล่าวร้าย หลอกลวง หยาบคาย ข่มขู่
              </p>
              <p class="rem5">8. ไม่ส่งต่อจดหมายลูกโซ่ หรือเมล์ขยะ</p>
              <p class="rem5">9. ถ้าสงสัยไม่ทำดีกว่า</p>
              <p class="rem5">
                10. รู้ไว้ด้วยว่าสำหรับผู้เขียน คือ บันทึกฉันท์เพื่อน
                แต่สำหรับผู้รับ คือ ข้อความที่จารึกไว้บนศิลาจารึก
              </p>
              <p class="rem5">11. ให้ความระมัดระวังกับคำเสียดสี และอารมณ์ขัน</p>
              <p class="rem5">
                12. อ่านข้อความในอีเมล์ ให้ละเอียดก่อนส่ง ความประณีตและตัวสะกด
                การันต์ เป็นสิ่งที่ควรคำนึงถึง
              </p>
              <p class="rem5">
                13. ดูรายชื่อผู้รับให้ดีว่า เขาคือคนที่เราตั้งใจจะส่งไปถึง
              </p>
